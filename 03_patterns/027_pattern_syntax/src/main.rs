// ============================================================================
// Урок 027. ОСНОВЫ СИНТАКСИСА ПАТТЕРНОВ
// ============================================================================
//
// 1. ЧТО ТАКОЕ ПАТТЕРН (pattern)?
//    Паттерн — это специальный синтаксис, который описывает ФОРМУ значения.
//    Когда мы "сопоставляем" значение с паттерном, компилятор проверяет,
//    подходит ли значение под эту форму, и заодно может вытащить из него
//    нужные части в новые переменные.
//
//    Простой пример:
//        let (x, y) = (10, 20);
//    Здесь `(x, y)` — это паттерн. Кортеж `(10, 20)` сопоставляется с ним,
//    и переменные `x` и `y` получают значения 10 и 20 соответственно.
//
// 2. ГДЕ ВСТРЕЧАЮТСЯ ПАТТЕРНЫ?
//    - в `match` (главное место)
//    - в `let` (`let (a, b) = ...`)
//    - в параметрах функций (`fn foo((x, y): (i32, i32))`)
//    - в `if let`, `while let`, `let ... else`  (см. уроки 031, 040)
//    - в циклах `for` (`for (k, v) in map { ... }`)
//
// 3. ОПРОВЕРЖИМЫЕ И НЕОПРОВЕРЖИМЫЕ ПАТТЕРНЫ
//    - НЕопровержимый (irrefutable) — паттерн, который ВСЕГДА подходит
//      под любое значение нужного типа. Его можно использовать в `let`
//      и параметрах функций.
//      Пример: `let x = 5;`  или  `let (a, b) = pair;`
//
//    - Опровержимый (refutable) — паттерн, который МОЖЕТ не совпасть.
//      Его можно использовать в `match`, `if let`, `while let`.
//      Пример: `Some(x)` для `Option<i32>` — может не совпасть, если значение `None`.
//
//    Если попытаться написать `let Some(x) = option_value;` — компилятор
//    откажется, потому что паттерн опровержимый.
//
// 4. ЧТО РАЗБИРАЕМ В ЭТОМ УРОКЕ:
//    - литералы как паттерны
//    - подстановочный знак `_` (wildcard)
//    - объединение через `|`
//    - range-паттерны (`1..=5`)
//    - игнорирование "хвоста" через `..`
//    - связывание (привязка значений к именам)
//    - различие "переменная" и "константа" внутри паттерна
//
//    БОЛЕЕ ПРОДВИНУТЫЕ ВЕЩИ — в следующих уроках:
//    - деструктуризация структур / enum / tuple ----------> урок 028
//    - match guards (if в ветке) и связывание `@`  ------> урок 029
//    - ref / ref mut и тонкости заимствования -----------> урок 030
//    - if let / while let / let ... else ----------------> урок 031
//
// ============================================================================

fn main() {
    println!("=== Урок 027. ОСНОВЫ СИНТАКСИСА ПАТТЕРНОВ ===\n");

    // ------------------------------------------------------------------
    // 1. ПАТТЕРН-ЛИТЕРАЛ
    // ------------------------------------------------------------------
    // Самый простой вид паттерна — конкретное значение (число, символ,
    // строковый литерал, true/false). Match просто сравнивает на равенство.
    println!("1. Паттерн-литерал:");

    let n = 3;
    match n {
        1 => println!("   один"),
        2 => println!("   два"),
        3 => println!("   три"),
        _ => println!("   что-то ещё"),
    }

    // Литералы работают и со строками, и с символами.
    let letter = 'b';
    match letter {
        'a' => println!("   буква a"),
        'b' => println!("   буква b"),
        _ => println!("   другая буква"),
    }

    // ------------------------------------------------------------------
    // 2. ПОДСТАНОВОЧНЫЙ ЗНАК `_` (wildcard)
    // ------------------------------------------------------------------
    // `_` — паттерн, который совпадает с ЧЕМ УГОДНО, но НЕ привязывает
    // значение к имени. Используют:
    //   - как "ловушку" в match для всех остальных случаев;
    //   - чтобы явно проигнорировать значение.
    println!("\n2. Wildcard `_`:");

    let code = 418;
    match code {
        200 => println!("   200 OK"),
        404 => println!("   404 Not Found"),
        _ => println!("   неизвестный код: (значение НЕ доступно — мы взяли `_`)"),
    }

    // `_` можно использовать и в `let`, чтобы выкинуть значение.
    let _ = compute_and_return_unused();
    // Без `_` компилятор предупредит: "unused result".

    // ------------------------------------------------------------------
    // 3. ОБЪЕДИНЕНИЕ ПАТТЕРНОВ ЧЕРЕЗ `|`
    // ------------------------------------------------------------------
    // `|` означает "ИЛИ". Ветка сработает, если значение подходит под
    // ЛЮБОЙ из перечисленных паттернов.
    println!("\n3. Объединение через `|`:");

    let day = 6;
    match day {
        1 | 2 | 3 | 4 | 5 => println!("   рабочий день ({})", day),
        6 | 7 => println!("   выходной ({})", day),
        _ => println!("   некорректный номер дня"),
    }

    // ------------------------------------------------------------------
    // 4. RANGE-ПАТТЕРНЫ
    // ------------------------------------------------------------------
    // В match можно использовать ВКЛЮЧАЮЩИЕ диапазоны: `start..=end`.
    // Эксклюзивный `start..end` в паттернах НЕстабилен и обычно
    // не используется (для сравнения чисел берите `..=` или guard).
    //
    // Range-паттерны работают для целых чисел и для `char`.
    println!("\n4. Range-паттерны:");

    let age: u8 = 25;
    match age {
        0..=12 => println!("   ребёнок ({} лет)", age),
        13..=19 => println!("   подросток ({} лет)", age),
        20..=64 => println!("   взрослый ({} лет)", age),
        _ => println!("   пожилой ({} лет)", age),
    }

    // Range для `char`.
    let ch = 'K';
    match ch {
        'a'..='z' => println!("   '{}' — строчная ��атинская", ch),
        'A'..='Z' => println!("   '{}' — заглавная латинская", ch),
        '0'..='9' => println!("   '{}' — цифра", ch),
        _ => println!("   '{}' — какой-то другой символ", ch),
    }

    // ------------------------------------------------------------------
    // 5. КОМБИНАЦИЯ `|` И ДИАПАЗОНОВ
    // ------------------------------------------------------------------
    // Паттерны можно складывать: один range или другой, литерал или диапазон.
    println!("\n5. Комбинации `|` и `..=`:");

    let score = 75;
    match score {
        0..=39 => println!("   {} — неуд", score),
        40..=59 | 60..=69 => println!("   {} — удовлетворительно", score),
        70..=89 => println!("   {} — хорошо", score),
        90..=100 => println!("   {} — отлично", score),
        _ => println!("   {} — некорректная оценка", score),
    }

    // ------------------------------------------------------------------
    // 6. ИГНОРИРОВАНИЕ ЧАСТЕЙ КОРТЕЖА: `_` И `..`
    // ------------------------------------------------------------------
    // `_` игнорирует ОДНО конкретное место.
    // `..` игнорирует "всё остальное" — может стоять только в ОДНОМ месте паттерна,
    // иначе компилятор не сможет понять, какие именно элементы пропустить.
    println!("\n6. Игнорирование `_` и `..` в кортежах:");

    let triple = (10, 20, 30);

    // Игнорируем средний элемент.
    let (a, _, c) = triple;
    println!("   первый = {}, последний = {}", a, c);

    // Берём только первый, остальное — в `..`.
    let (first, ..) = triple;
    println!("   только первый = {}", first);

    // Берём только последний.
    let (.., last) = triple;
    println!("   только последний = {}", last);

    // Берём края, середину выкидываем.
    let big_tuple = (1, 2, 3, 4, 5);
    let (start, .., end) = big_tuple;
    println!("   start = {}, end = {}", start, end);

    // ------------------------------------------------------------------
    // 7. `..` В МАССИВАХ И СРЕЗАХ
    // ------------------------------------------------------------------
    // Тот же `..` отлично работает с массивами и срезами в `match`.
    println!("\n7. `..` в массивах:");

    let nums = [1, 2, 3, 4, 5];
    match nums {
        [1, ..] => println!("   массив начинается с 1: {:?}", nums),
        _ => println!("   массив не начинается с 1"),
    }

    match nums {
        [first, .., last] => println!("   первый = {}, последний = {}", first, last),
    }

    // Можно зафиксировать первые несколько элементов, а хвост выкинуть.
    let pixels = [255u8, 0, 0, 128, 255];
    match pixels {
        [r, g, b, ..] => println!("   RGB = ({}, {}, {})", r, g, b),
    }

    // ------------------------------------------------------------------
    // 8. СВЯЗЫВАНИЕ ПЕРЕМЕННЫХ В ПАТТЕРНЕ
    // ------------------------------------------------------------------
    // Паттерны не только проверяют форму, но и ПРИВЯЗЫВАЮТ части значения
    // к новым именам. Имя в паттерне = новая переменная.
    println!("\n8. Связывание переменных:");

    let point = (3, -7);
    match point {
        // x и y — это НЕ сравнение со старыми x/y, а НОВЫЕ переменные.
        (x, y) => println!("   связали: x = {}, y = {}", x, y),
    }

    // Можно частично фиксировать значения, а частично связывать.
    let pair = (0, 42);
    match pair {
        (0, y) => println!("   первый ноль, второй = {}", y),
        (x, 0) => println!("   второй ноль, первый = {}", x),
        (x, y) => println!("   произвольная пара ({}, {})", x, y),
    }

    // ------------------------------------------------------------------
    // 9. ВАЖНЫЙ НЮАНС: ПЕРЕМЕННАЯ vs КОНСТАНТА В ПАТТЕРНЕ
    // ------------------------------------------------------------------
    // Если в паттерне написать имя СТРОЧНЫМИ буквами — это создаст
    // НОВУЮ ПЕРЕМЕННУЮ и затенит внешнюю с тем же именем.
    // Если хочется сравнить с внешним значением — используйте `const`,
    // полный путь enum-варианта или match guard (`if x == внешнее`).
    println!("\n9. Переменная vs константа в паттерне:");

    let target = 5;
    let value = 10;

    match value {
        // ВНИМАНИЕ: здесь `target` — это НОВАЯ переменная, в которую
        // записывается `value`. Сравнения со внешним `target` НЕ происходит.
        target => println!(
            "   паттерн `target` поймал {} — это shadowing, а не сравнение!",
            target
        ),
    }

    // Правильный способ сравнить со значением переменной — guard
    // (подробно в уроке 029):
    match value {
        v if v == target => println!("   {} равно target ({})", v, target),
        v => println!("   {} не равно target ({})", v, target),
    }

    // А вот с КОНСТАНТАМИ всё работает "как ожидаешь" — они сравниваются:
    const MAX: i32 = 100;
    let n = 100;
    match n {
        MAX => println!("   {} == MAX (константа сравнилась)", n),
        _ => println!("   {} != MAX", n),
    }

    // ------------------------------------------------------------------
    // 10. ПАТТЕРНЫ В `let` И ПАРАМЕТРАХ ФУНКЦИЙ (мини-обзор)
    // ------------------------------------------------------------------
    // Здесь — только короткий взгляд: подробная деструктуризация будет
    // в уроке 028. Главное правило: в `let` и параметрах допустимы только
    // НЕопровержимые паттерны.
    println!("\n10. Паттерны вне match:");

    // let с кортежем (неопровержимый паттерн).
    let (lat, lon) = (55.75, 37.62);
    println!("   координаты: {}, {}", lat, lon);

    // Параметр-кортеж: тоже паттерн.
    fn distance_from_origin((x, y): (f64, f64)) -> f64 {
        (x * x + y * y).sqrt()
    }
    println!(
        "   расстояние до (3, 4): {}",
        distance_from_origin((3.0, 4.0))
    );

    println!("\n=== КОНЕЦ УРОКА ===");
}

// Вспомогательная функция для демонстрации `let _ = ...`.
fn compute_and_return_unused() -> i32 {
    42
}

// ============================================================================
// КЛЮЧЕВЫЕ МОМЕНТЫ:
// ============================================================================
// ✓ Паттерн = "форма" значения. При совпадении могут привязываться переменные.
// ✓ `_` совпадает с чем угодно и НИЧЕГО не привязывает.
// ✓ `|` — это "ИЛИ" между паттернами в одной ветке.
// ✓ `start..=end` — диапазон ВКЛЮЧИТЕЛЬНО (для чисел и char).
// ✓ `..` пропускает "всё остальное" в кортеже / массиве / срезе.
// ✓ Имя в паттерне создаёт НОВУЮ переменную (shadowing), а не сравнение
//   с внешней переменной — для сравнения берите const или match guard.
// ✓ В `let` и параметрах функций можно использовать только НЕопровержимые паттерны.
