// ============================================================================
// Урок 029. MATCH GUARDS И @-BINDING
// Match guard — дополнительное условие if после паттерна в ветке match.
// @-binding — способ одновременно проверить паттерн и сохранить значение.
// ============================================================================

fn main() {
    println!("=== Урок 029. MATCH GUARDS И @-BINDING ===\n");

    // ------------------------------------------------------------------------
    // 1. БАЗОВЫЙ СИНТАКСИС MATCH GUARD
    // Guard — это if-условие после паттерна: pattern if condition => ...
    // Ветка срабатывает только если и паттерн совпал, и условие истинно.
    // ------------------------------------------------------------------------
    println!("1. Базовый синтаксис match guard:");

    let num = 7;
    match num {
        n if n < 0 => println!("   {} — отрицательное", n),
        n if n == 0 => println!("   нуль"),
        n if n % 2 == 0 => println!("   {} — чётное положительное", n),
        n => println!("   {} — нечётное положительное", n),
    }

    // ------------------------------------------------------------------------
    // 2. GUARD ПРОВЕРЯЕТ ЗНАЧЕНИЕ, СВЯЗАННОЕ В ПАТТЕРНЕ
    // Переменная, захваченная паттерном, доступна в guard-условии.
    // ------------------------------------------------------------------------
    println!("\n2. Guard использует переменную из паттерна:");

    let pair = (3, 9);
    match pair {
        (x, y) if x == y => println!("   Числа равны: {}", x),
        (x, y) if x * x == y => println!("   {} в квадрате равно {}", x, y),
        (x, y) if x + y == 0 => println!("   Числа противоположны: {} и {}", x, y),
        (x, y) => println!("   Просто пара: ({}, {})", x, y),
    }

    // ------------------------------------------------------------------------
    // 3. GUARD С НЕСКОЛЬКИМИ ПАТТЕРНАМИ ЧЕРЕЗ |
    // Guard применяется ко ВСЕМ паттернам через |, а не только к последнему.
    // Приоритет: (A | B) if cond  —  скобки расставляются именно так.
    // ------------------------------------------------------------------------
    println!("\n3. Guard с паттернами через |:");

    let x = 4;
    match x {
        // guard применяется к обоим значениям 2 и 4
        2 | 4 | 6 | 8 if x < 5 => println!("   {} — чётное и меньше 5", x),
        2 | 4 | 6 | 8 => println!("   {} — чётное, не меньше 5", x),
        _ => println!("   {} — нечётное", x),
    }

    // ------------------------------------------------------------------------
    // 4. GUARD С ВНЕШНЕЙ ПЕРЕМЕННОЙ
    // Guard может ссылаться на переменные из внешней области видимости.
    // Паттерны (кроме литералов) НЕ сравниваются с внешними переменными —
    // они создают новую привязку! Guard — единственный способ сравнить.
    // ------------------------------------------------------------------------
    println!("\n4. Guard сравнивает с внешней переменной:");

    let target = 42;
    let numbers = vec![10, 42, 7, 42, 99];

    for &n in &numbers {
        match n {
            // паттерн `n` создаёт привязку, guard сравнивает с target
            val if val == target => println!("   {} — это target!", val),
            val => println!("   {} — не target", val),
        }
    }

    // Ловушка: вот что НЕ работает как ожидается:
    // match n { target => ... }  — target здесь станет новой переменной, совпадёт всегда!
    // Правильно — только через guard: n if n == target

    // ------------------------------------------------------------------------
    // 5. GUARD С ENUM
    // Guard отлично работает совместно с деструктуризацией enum.
    // ------------------------------------------------------------------------
    println!("\n5. Guard с enum:");

    #[allow(dead_code)]
    enum Command {
        Move { x: i32, y: i32 },
        Fire { damage: u32 },
        Heal(u32),
        Wait,
    }

    let cmd = Command::Move { x: -5, y: 3 };

    match cmd {
        Command::Move { x, y } if x == 0 && y == 0 => {
            println!("   Команда Move: остаёмся на месте");
        }
        Command::Move { x, y } if x < 0 => {
            println!("   Команда Move: идём влево на {}, вверх на {}", x.abs(), y);
        }
        Command::Move { x, y } => {
            println!("   Команда Move: ({}, {})", x, y);
        }
        Command::Fire { damage } if damage > 50 => {
            println!("   Мощный выстрел! Урон: {}", damage);
        }
        Command::Fire { damage } => {
            println!("   Выстрел. Урон: {}", damage);
        }
        Command::Heal(hp) if hp == 0 => {
            println!("   Лечение на 0 — бесполезно");
        }
        Command::Heal(hp) => {
            println!("   Лечение: +{} HP", hp);
        }
        Command::Wait => {
            println!("   Ждём...");
        }
    }

    // ------------------------------------------------------------------------
    // 6. GUARD С OPTION И RESULT
    // ------------------------------------------------------------------------
    println!("\n6. Guard с Option и Result:");

    let values: Vec<Option<i32>> = vec![Some(10), None, Some(-5), Some(0), Some(100)];

    for val in &values {
        match val {
            Some(n) if *n > 50 => println!("   Большое число: {}", n),
            Some(n) if *n < 0 => println!("   Отрицательное: {}", n),
            Some(0) => println!("   Нуль"),
            Some(n) => println!("   Обычное число: {}", n),
            None => println!("   Пусто"),
        }
    }

    let results: Vec<Result<i32, &str>> = vec![Ok(200), Err("timeout"), Ok(404), Ok(0)];

    for res in &results {
        match res {
            Ok(code) if *code >= 200 && *code < 300 => println!("   Успех: {}", code),
            Ok(code) if *code >= 400 => println!("   Ошибка клиента: {}", code),
            Ok(code) => println!("   Другой код: {}", code),
            Err(e) => println!("   Ошибка сети: {}", e),
        }
    }

    // ========================================================================
    // @-BINDING
    // @ позволяет одновременно:
    //   1. проверить, что значение совпадает с паттерном (диапазон, литерал и т.д.)
    //   2. сохранить это значение в переменную
    // Синтаксис: variable @ pattern
    // ========================================================================

    // ------------------------------------------------------------------------
    // 7. БАЗОВЫЙ @-BINDING С ДИАПАЗОНОМ
    // ------------------------------------------------------------------------
    println!("\n7. Базовый @-binding с диапазоном:");

    let id = 7;
    match id {
        user_id @ 1..=10 => println!("   ID {} в допустимом диапазоне (1–10)", user_id),
        user_id @ 11..=99 => println!("   ID {} в расширенном диапазоне (11–99)", user_id),
        _ => println!("   ID вне диапазона"),
    }

    // Без @-binding мы не смогли бы использовать значение внутри ветки:
    // 1..=10 => println!("{}", ???)  — значение неизвестно!

    // ------------------------------------------------------------------------
    // 8. @-BINDING В РАЗНЫХ КОНТЕКСТАХ
    // ------------------------------------------------------------------------
    println!("\n8. @-binding в разных контекстах:");

    // С перечислением через |
    let grade = 85;
    let description = match grade {
        g @ 90..=100 => format!("   Отлично ({})", g),
        g @ 75..=89 => format!("   Хорошо ({})", g),
        g @ 60..=74 => format!("   Удовлетворительно ({})", g),
        g => format!("   Неудовлетворительно ({})", g),
    };
    println!("{}", description);

    // С Option
    let maybe_score: Option<u32> = Some(95);
    match maybe_score {
        Some(score @ 90..=100) => println!("   Высший балл: {}", score),
        Some(score @ 60..=89) => println!("   Зачёт: {}", score),
        Some(score) => println!("   Незачёт: {}", score),
        None => println!("   Оценки нет"),
    }

    // ------------------------------------------------------------------------
    // 9. @-BINDING С ENUM
    // ------------------------------------------------------------------------
    println!("\n9. @-binding с enum:");

    #[allow(dead_code)]
    #[derive(Debug)]
    enum Packet {
        Data(Vec<u8>),
        Ack(u32),
        Error(u32),
    }

    let packet = Packet::Ack(1024);

    match packet {
        Packet::Data(ref bytes) if bytes.is_empty() => {
            println!("   Пустой Data-пакет");
        }
        Packet::Data(ref bytes) => {
            println!("   Data-пакет: {} байт", bytes.len());
        }
        // @-binding сохраняет весь enum-вариант в переменную
        p @ Packet::Ack(_) => {
            println!("   Подтверждение получено: {:?}", p);
        }
        Packet::Error(code @ 500..=599) => {
            println!("   Критическая ошибка сервера: {}", code);
        }
        Packet::Error(code) => {
            println!("   Ошибка: {}", code);
        }
    }

    // ------------------------------------------------------------------------
    // 10. @-BINDING + GUARD ВМЕСТЕ
    // Можно комбинировать: захватить значение через @ и добавить guard.
    // ------------------------------------------------------------------------
    println!("\n10. @-binding совместно с guard:");

    let temperature: i32 = -15;

    match temperature {
        t @ i32::MIN..=-1 if t < -10 => {
            println!("   {}°C — сильный мороз, одевайтесь теплее!", t);
        }
        t @ i32::MIN..=-1 => {
            println!("   {}°C — небольшой мороз", t);
        }
        0 => println!("   0°C — точка замерзания воды"),
        t @ 1..=15 => println!("   {}°C — прохладно", t),
        t @ 16..=25 => println!("   {}°C — комфортно", t),
        t => println!("   {}°C — жарко!", t),
    }

    // ------------------------------------------------------------------------
    // 11. ПРАКТИЧЕСКИЙ ПРИМЕР: КЛАССИФИКАТОР HTTP-ЗАПРОСОВ
    // ------------------------------------------------------------------------
    println!("\n11. Практический пример — классификатор HTTP-ответов:");

    #[derive(Debug)]
    struct HttpResponse {
        status: u16,
        body_size: usize,
    }

    let responses = vec![
        HttpResponse { status: 200, body_size: 1024 },
        HttpResponse { status: 201, body_size: 0 },
        HttpResponse { status: 301, body_size: 0 },
        HttpResponse { status: 404, body_size: 128 },
        HttpResponse { status: 500, body_size: 256 },
        HttpResponse { status: 503, body_size: 64 },
    ];

    for resp in &responses {
        let label = match resp {
            HttpResponse { status: code @ 200..=299, body_size } if *body_size > 0 => {
                format!("   [{}] Успех с телом ({} байт)", code, body_size)
            }
            HttpResponse { status: code @ 200..=299, .. } => {
                format!("   [{}] Успех без тела", code)
            }
            HttpResponse { status: code @ 300..=399, .. } => {
                format!("   [{}] Перенаправление", code)
            }
            HttpResponse { status: code @ 400..=499, body_size } => {
                format!("   [{}] Ошибка клиента (тело: {} байт)", code, body_size)
            }
            HttpResponse { status: code @ 500..=599, .. } => {
                format!("   [{}] Ошибка сервера", code)
            }
            HttpResponse { status, .. } => {
                format!("   [{}] Неизвестный статус", status)
            }
        };
        println!("{}", label);
    }

    println!("\n=== КОНЕЦ УРОКА ===");
}

// ============================================================================
// КЛЮЧЕВЫЕ МОМЕНТЫ:
// ============================================================================
// MATCH GUARD (if-условие в ветке):
// ✓ Синтаксис: pattern if condition => body
// ✓ Guard применяется ко ВСЕМ паттернам через | — не только к последнему
// ✓ Единственный способ сравнить захваченную переменную с внешней переменной:
//   переменные в паттерне ВСЕГДА создают новую привязку, а не сравнивают!
// ✓ Guard может ссылаться на переменные из паттерна и из внешней области
//
// @-BINDING (захват + проверка одновременно):
// ✓ Синтаксис: variable @ pattern
// ✓ Захватывает значение в переменную И проверяет что оно совпадает с паттерном
// ✓ Без @ при диапазоне мы не знаем конкретное значение внутри ветки
// ✓ Работает с диапазонами, enum-вариантами, литералами
// ✓ Можно комбинировать: variable @ pattern if condition
// ============================================================================
