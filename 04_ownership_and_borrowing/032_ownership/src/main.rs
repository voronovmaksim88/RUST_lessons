// Забирает владение строкой. После вызова у вызывающего строки больше нет.
// В конце функции параметр s уходит из области видимости — строка освобождается.
fn takes_ownership(s: String) {
    println!("   takes_ownership получил: \"{}\"", s);
} // <- здесь s умирает, память строки освобождена

// Создаёт значение и ОТДАЁТ владение наружу через return.
fn gives_ownership() -> String {
    String::from("создано внутри функции")
}

// Паттерн "принять и вернуть": забирает владение, меняет, возвращает.
fn process(mut s: String) -> String {
    s.push_str(", лети!");
    s // владение уходит обратно вызывающему
}

// Неудобный способ "только посмотреть": возвращаем значение вместе с результатом.
// Именно эту проблему решают ссылки (урок 033).
fn calculate_length(s: String) -> (String, usize) {
    let len = s.len();
    (s, len)
}

// ----------------------------------------------------------------------------
// Структура и функция для раздела 8
// ----------------------------------------------------------------------------

struct User {
    name: String,
    email: String,
}

// Параметры name и email перемещаются в поля создаваемой структуры
// (сокращённая инициализация — урок 023).
fn create_user(name: String, email: String) -> User {
    User { name, email }
}
