// ============================================================================
// Урок 032. ВЛАДЕНИЕ (OWNERSHIP)
// ============================================================================
//
// Владение — центральная концепция Rust: язык управляет памятью без сборщика
// мусора и без ручного free(), проверяя всё на этапе КОМПИЛЯЦИИ.
//
// Предварительные знания: String (урок 018), структуры (023), Vec (026).
//
// Границы урока (эти темы — в соседних уроках, здесь только упоминаем):
//   - ссылки и заимствование (&, &mut, правила)  -> урок 033
//   - срезы                                      -> урок 034
//   - времена жизни                              -> урок 035
//   - String vs &str в деталях                   -> урок 036
//   - Copy vs Clone в деталях                    -> урок 037
// ============================================================================

fn main() {
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║            Урок 032. ВЛАДЕНИЕ (OWNERSHIP)                   ║");
    println!("╚══════════════════════════════════════════════════════════════╝");

    // ============================================
    // 1. ЗАЧЕМ НУЖНО ВЛАДЕНИЕ
    // ============================================
    println!("\n1. Зачем нужно владение");

    // Три подхода к управлению памятью в языках программирования:
    //
    //   1) Вручную (C/C++): программист сам вызывает malloc/free.
    //      Быстро, но легко ошибиться: забыл free — утечка,
    //      освободил дважды — double-free, использовал после
    //      освобождения — use-after-free (краши и уязвимости).
    //
    //   2) Сборщик мусора / GC (Python, Go, Java): runtime сам находит
    //      ненужные объекты. Безопасно, но есть накладные расходы
    //      и непредсказуемые паузы.
    //
    //   3) Владение (Rust): компилятор по правилам владения ТОЧНО знает,
    //      в какой момент значение перестаёт быть нужным, и вставляет
    //      освобождение памяти сам. Проверки — на этапе компиляции,
    //      в runtime — ноль дополнительных затрат.

    println!("   C/C++  — вручную: быстро, но опасно (утечки, double-free)");
    println!("   GC     — безопасно, но платим паузами и памятью");
    println!("   Rust   — владение: безопасно И без затрат в runtime");

    // Напоминание про стек и кучу (важно для понимания move):
    //
    //   let x: i32 = 5;                 — значение целиком на СТЕКЕ
    //   let s = String::from("привет"); — на стеке лежит "заголовок"
    //                                     (указатель, длина, ёмкость),
    //                                     а сами байты текста — в КУЧЕ
    //
    // Стек освобождается автоматически при выходе из функции.
    // А вот кучей нужно управлять — именно для этого и нужно владение.

    // ============================================
    // 2. ТРИ ПРАВИЛА ВЛАДЕНИЯ
    // ============================================
    println!("\n2. Три правила владения");
    println!("   1) У каждого значения есть владелец (переменная)");
    println!("   2) Владелец может быть только ОДИН");
    println!("   3) Когда владелец уходит из области видимости — значение освобождается");

    // Правило 1: переменная s — владелец строки.
    let s = String::from("у меня есть владелец");
    println!("   Пример: s владеет строкой \"{}\"", s);

    // Правило 2: после `let other = s;` владелец станет other, а s — недействителен.
    // (подробно — в разделе 4 про move)

    // Правило 3: в конце блока {} владелец умирает и память освобождается.
    {
        let inner = String::from("живу только внутри блока");
        println!("   Внутри блока: \"{}\"", inner);
    } // <- здесь inner уходит из области видимости, память строки освобождена
    // println!("{}", inner); // ОШИБКА: cannot find value `inner` in this scope

    // ============================================
    // 3. ОБЛАСТЬ ВИДИМОСТИ И DROP
    // ============================================
    println!("\n3. Область видимости и Drop");

    // Когда владелец уходит из области видимости, Rust вызывает для значения
    // специальный механизм Drop — он и освобождает память. Это называется RAII:
    // «получение ресурса = инициализация, конец жизни = освобождение».
    //
    // Чтобы УВИДЕТЬ момент освобождения, создадим структуру с собственным
    // сообщением при уничтожении. (Drop — это трейт; трейты подробно
    // разбираются в уроке 054, здесь просто пользуемся механизмом.)

    struct Resource {
        name: &'static str,
    }

    impl Drop for Resource {
        fn drop(&mut self) {
            println!("   [drop] ресурс '{}' освобождён", self.name);
        }
    }

    println!("   Создаём ресурсы a, b и блочный inner:");
    let _a = Resource { name: "a" };
    let _b = Resource { name: "b" };

    {
        let _inner = Resource { name: "inner (из вложенного блока)" };
        println!("   ...конец вложенного блока:");
    } // <- _inner умирает здесь, раньше чем _a и _b

    // Досрочное освобождение: std::mem::drop передаёт владение функции drop,
    // и значение уничтожается немедленно, не дожидаясь конца блока.
    let early = Resource { name: "early (drop вручную)" };
    println!("   Вызываем drop(early) досрочно:");
    drop(early);
    // println!("{}", early.name); // ОШИБКА: value borrowed here after move

    println!("   ...конец main: оставшиеся ресурсы умрут в ОБРАТНОМ порядке (b, a)");
    println!("   (их сообщения [drop] появятся в САМОМ КОНЦЕ вывода, после ✅!)");
    // Порядок drop в конце блока — обратный порядку объявления:
    // последним создан — первым уничтожен (как стек).

    // ============================================
    // 4. ПЕРЕМЕЩЕНИЕ (MOVE)
    // ============================================
    println!("\n4. Перемещение (move)");

    // Что происходит при `let s2 = s1;` для String?
    //
    //   ДО:                                ПОСЛЕ let s2 = s1;
    //
    //   s1 ──> [ptr|len=6|cap=6]           s1   ✗ (недействителен)
    //              │                       s2 ──> [ptr|len=6|cap=6]
    //              ▼                                  │
    //   куча:  "привет"                               ▼
    //                                      куча:  "привет"  (та же память!)
    //
    // Копируется только "заголовок" (указатель, длина, ёмкость) — БАЙТЫ В КУЧЕ
    // НЕ КОПИРУЮТСЯ. Владелец теперь s2, а s1 объявляется недействительным.
    //
    // Почему нельзя оставить обоих? Если бы s1 и s2 оба считались владельцами,
    // в конце блока Rust освободил бы одну и ту же память ДВАЖДЫ (double-free).
    // Правило "владелец один" исключает это на уровне компилятора.

    let s1 = String::from("привет");
    let s2 = s1; // <- MOVE: владение перешло к s2

    // println!("{}", s1); // ОШИБКА КОМПИЛЯЦИИ: borrow of moved value: `s1`
    println!("   s2 = \"{}\" (s1 больше недействителен)", s2);

    // Move происходит не только при присваивании:
    //   - при передаче значения В функцию        (раздел 6)
    //   - при возврате значения ИЗ функции       (раздел 6)
    //   - при деструктуризации (видели в уроке 028: let (name, age) = person;)
    //   - при помещении в коллекцию или структуру (раздел 7)

    // ============================================
    // 5. COPY-ТИПЫ: КОГДА MOVE НЕ ПРОИСХОДИТ (КРАТКО)
    // ============================================
    println!("\n5. Copy-типы: когда move не происходит");

    // Числа, bool, char живут целиком на стеке — их дёшево скопировать.
    // Такие типы реализуют Copy: при присваивании создаётся независимая
    // копия, и оригинал ОСТАЁТСЯ действительным.
    let x: i32 = 42;
    let y = x; // копия, а не move
    println!("   x = {}, y = {} — оба живы (i32 — Copy)", x, y);

    // Это уже встречалось раньше:
    //   - урок 012: for n in numbers — массив [i32; 5] скопировался
    //   - урок 021: кортеж из Copy-типов копируется целиком
    //
    // String, Vec и другие "владельцы данных в куче" Copy НЕ реализуют —
    // для них работает move. Если нужна настоящая копия — clone():
    let original = String::from("оригинал");
    let copy = original.clone(); // явная глубокая копия (байты в куче дублируются)
    println!("   original = \"{}\", copy = \"{}\" — оба живы (clone)", original, copy);

    // Подробное сравнение Copy vs Clone (и когда Copy можно derive) — урок 037.

    // ============================================
    // 6. ВЛАДЕНИЕ И ФУНКЦИИ
    // ============================================
    println!("\n6. Владение и функции");

    // Передача аргумента по значению — это тоже MOVE (для не-Copy типов).
    let gift = String::from("подарок для функции");
    takes_ownership(gift); // владение ушло внутрь функции
    // println!("{}", gift); // ОШИБКА: borrow of moved value: `gift`
    println!("   После вызова takes_ownership переменная gift недоступна");

    // В конце takes_ownership её параметр уходит из области видимости —
    // и строка освобождается. Функция "поглотила" значение.

    // Возврат значения — передача владения наружу:
    let received = gives_ownership();
    println!("   Получили владение из функции: \"{}\"", received);

    // Паттерн "принять и вернуть": функция забирает значение,
    // меняет его и отдаёт владение обратно.
    let s = String::from("шарик");
    let s = process(s); // владение ушло и вернулось (заодно shadowing из урока 004)
    println!("   После process: \"{}\"", s);

    // НЕУДОБСТВО: а если функция должна только ПОСМОТРЕТЬ на значение?
    // Приходится возвращать его обратно вместе с результатом — кортежем:
    let text = String::from("сколько во мне байт?");
    let (text, len) = calculate_length(text); // вернули и строку, и длину
    println!("   \"{}\" — {} байт", text, len);
    println!("   Громоздко! Решение — ссылки (&), это урок 033");

    // ============================================
    // 7. ВЛАДЕНИЕ В СТРУКТУРАХ И КОЛЛЕКЦИЯХ
    // ============================================
    println!("\n7. Владение в структурах и коллекциях");

    // Структура владеет своими полями.
    struct Profile {
        nickname: String,
        score: u32,
    }

    let profile = Profile {
        nickname: String::from("rustacean"),
        score: 100,
    };

    // Можно переместить ОДНО поле наружу — это частичное перемещение
    // (partial move): структура целиком становится недоступной,
    // но НЕ тронутые Copy-поля читать ещё можно.
    let nickname = profile.nickname; // move поля String
    println!("   Забрали поле nickname: \"{}\"", nickname);
    println!("   Copy-поле ещё доступно: score = {}", profile.score);
    // println!("{}", profile.nickname); // ОШИБКА: value borrowed after partial move

    // Коллекции тоже владеют элементами: push забирает владение.
    let first = String::from("первый");
    let mut storage: Vec<String> = Vec::new();
    storage.push(first); // владение строки ушло в вектор
    // println!("{}", first); // ОШИБКА: borrow of moved value: `first`
    println!("   После push строка принадлежит вектору: {:?}", storage);

    // Забрать владение обратно можно через pop() (возвращает Option<String>):
    let taken = storage.pop();
    println!("   pop() вернул владение: {:?}", taken);

    // Теперь понятно, почему `for x in vec` потребляет вектор (урок 012):
    // цикл по значениям ЗАБИРАЕТ владение каждым элементом по очереди,
    // и в конце вектор пуст и недействителен.
    let words = vec![String::from("раз"), String::from("два"), String::from("три")];
    print!("   for w in words: ");
    for w in words {
        print!("[{}] ", w); // w — владелец элемента, умирает на каждой итерации
    }
    println!();
    // println!("{:?}", words); // ОШИБКА: borrow of moved value: `words`

    // ============================================
    // 8. ПРАКТИКА: РЕГИСТРАТОР ПОЛЬЗОВАТЕЛЕЙ
    // ============================================
    println!("\n8. Практика: регистратор пользователей");

    // Сквозной пример: владение передаётся по цепочке
    // String -> структура User -> Vec<User>.

    let mut registry: Vec<User> = Vec::new();

    let name = String::from("Анна");
    let email = String::from("anna@example.com");

    // name и email ПЕРЕМЕЩАЮТСЯ в функцию, оттуда — в поля User:
    let user = create_user(name, email);
    // println!("{}", name); // ОШИБКА: name перемещён в create_user

    println!("   Создан пользователь: {} <{}>", user.name, user.email);

    // user перемещается в вектор — теперь им владеет registry:
    registry.push(user);
    // println!("{}", user.name); // ОШИБКА: user перемещён в registry

    // Добавим ещё одного (всю цепочку move — одной строкой):
    registry.push(create_user(
        String::from("Борис"),
        String::from("boris@example.com"),
    ));

    println!("   В реестре {} пользователя(ей)", registry.len());

    // В конце потребляем вектор циклом по значениям —
    // владение каждым User по очереди переходит в переменную цикла:
    for user in registry {
        println!("   -> {} <{}>", user.name, user.email);
    } // каждый user (и его String-поля) освобождён здесь

    // ============================================
    // РЕЗЮМЕ
    // ============================================
    println!("\n╔══════════════════════════════════════════════════════════════╗");
    println!("║                     РЕЗЮМЕ                                  ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  Три правила: один владелец у значения; владелец один;      ║");
    println!("║  вышел из области видимости — значение освобождено (drop)   ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  move: при присваивании, передаче в функцию, возврате,      ║");
    println!("║  деструктуризации и помещении в структуру/коллекцию         ║");
    println!("║  Copy-типы (числа, bool, char) копируются — оригинал жив    ║");
    println!("║  clone() — явная глубокая копия (детали: урок 037)          ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  drop — в конце блока, в обратном порядке объявления        ║");
    println!("║  drop(x) — досрочное освобождение                           ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  Возвращать владение из каждой функции громоздко —          ║");
    println!("║  решение: ссылки и заимствование, урок 033                  ║");
    println!("╚══════════════════════════════════════════════════════════════╝");

    println!("\n✅ Урок по владению (ownership) завершён!");
}

