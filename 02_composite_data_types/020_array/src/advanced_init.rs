pub fn run() {
    // ========================================================================
    // 15. ПРОДВИНУТАЯ ИНИЦИАЛИЗАЦИЯ
    // ========================================================================
    println!("┌── 15.1 Инициализация с помощью from_fn() ────────────────────────────┐");

    // Создание массива через from_fn:
    // - from_fn вызывает замыкание для каждого индекса 0..N
    // - i — это индекс элемента, а не его значение
    // - возвращаемое значение становится элементом массива
    // Здесь создаём квадраты индексов: [0^2, 1^2, 2^2, 3^2, 4^2]
    let squares: [i32; 5] = std::array::from_fn(|i| (i * i) as i32);
    println!("  квадраты [0..5]: {:?}", squares);

    let fibonacci: [i32; 8] = std::array::from_fn(|i| match i {
        0 => 1,
        1 => 1,
        _ => {
            // Упрощённая версия для демонстрации
            let seq = [1, 1, 2, 3, 5, 8, 13, 21];
            seq[i]
        }
    });
    println!("  фибоначчи: {:?}", fibonacci);

    let evens: [i32; 6] = std::array::from_fn(|i| (i * 2) as i32);
    println!("  чётные числа: {:?}", evens);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
