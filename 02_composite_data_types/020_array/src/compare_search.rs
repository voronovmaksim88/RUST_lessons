pub fn run() {
    // ========================================================================
    // 16. СРАВНЕНИЕ И ПОИСК
    // ========================================================================
    println!("┌── 16.1 Сравнение массивов ───────────────────────────────────────────┐");
    let a1 = [1, 2, 3];
    let a2 = [1, 2, 3];
    let a3 = [1, 2, 4];

    println!("  [1,2,3] == [1,2,3]: {}", a1 == a2);
    println!("  [1,2,3] == [1,2,4]: {}", a1 == a3);
    println!("  [1,2,3] < [1,2,4]: {}", a1 < a3);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 16.1.1 Разыменовывание в замыканиях ───────────────────────────────┐");
    let deref_arr = [10, 20, 30];
    // iter() возвращает &i32, поэтому замыкание получает ссылку.
    // Вариант 1: разыменование через *x
    let pos_star = deref_arr.iter().position(|x| *x == 20);
    println!("  position(|x| *x == 20) => {:?}", pos_star);
    // Вариант 2: шаблон |&x| сразу разыменовывает ссылку в аргументе
    let pos_pattern = deref_arr.iter().position(|&x| x == 20);
    println!("  position(|&x| x == 20) => {:?}", pos_pattern);
    println!("  Оба варианта эквивалентны, |&x| короче и читается проще");
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 16.2 Поиск элементов ──────────────────────────────────────────────┐");
    let search_arr = [10, 20, 30, 40, 50];

    // Поиск позиции элемента:
    // position() возвращает Option<usize> — индекс первого найденного элемента
    // Замыкание получает ссылку &i32, поэтому используем |&x| для разыменования
    let position = search_arr.iter().position(|&x| x == 30);
    println!("  позиция элемента 30: {:?}", position);

    // Если элемент не найден, возвращается None
    let not_found = search_arr.iter().position(|&x| x == 100);
    println!("  позиция элемента 100: {:?}", not_found);

    // Поиск с условием:
    // find() возвращает Option<&i32> — ссылку на первый элемент, удовлетворяющий условию
    // Здесь используем |&&x|, потому что iter() даёт &i32, а find() передаёт &&i32
    let first_big = search_arr.iter().find(|&&x| x > 25);
    println!("  первый элемент > 25: {:?}", first_big);

    // Подсчёт элементов:
    // filter() оставляет только элементы >= 30, затем count() считает их количество
    let count = search_arr.iter().filter(|&&x| x >= 30).count();
    println!("  количество элементов >= 30: {}", count);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 16.3 Проверки массива ─────────────────────────────────────────────┐");
    let check_arr = [2, 4, 6, 8, 10];

    // Все элементы чётные?
    let all_even = check_arr.iter().all(|&x| x % 2 == 0);
    println!("  все элементы чётные: {}", all_even);

    // Есть ли элемент > 5?
    let any_greater_5 = check_arr.iter().any(|&x| x > 5);
    println!("  есть элемент > 5: {}", any_greater_5);

    // Пустой ли массив?
    println!("  массив пустой: {}", check_arr.is_empty());
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
