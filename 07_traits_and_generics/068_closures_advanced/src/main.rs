// РАЗДЕЛ 7. ПРАКТИКА: КОНВЕЙЕР ОБРАБОТКИ ДАННЫХ
// ============================================================================
fn section_07_practice() {
    println!("\n── 7. Практика: конвейер обработки данных ─────────────────────────");

    // Данные: список транзакций (сумма, категория, одобрена ли)
    #[derive(Debug)]
    struct Transaction {
        amount: f64,
        category: &'static str,
        approved: bool,
    }

    let transactions = vec![
        Transaction { amount: 120.0, category: "еда",      approved: true  },
        Transaction { amount: 850.0, category: "техника",  approved: true  },
        Transaction { amount: 30.0,  category: "еда",      approved: false },
        Transaction { amount: 2400.0,category: "техника",  approved: true  },
        Transaction { amount: 15.0,  category: "книги",    approved: true  },
        Transaction { amount: 99.0,  category: "книги",    approved: false },
        Transaction { amount: 500.0, category: "техника",  approved: true  },
    ];

    // Задача 1: сумма одобренных транзакций в категории "техника"
    let tech_total: f64 = transactions.iter()
        .filter(|t| t.approved && t.category == "техника")
        .map(|t| t.amount)
        .sum();
    println!("  техника (одобрено): {:.2} руб.", tech_total);

    // Задача 2: топ-3 транзакции по сумме среди одобренных
    let mut approved_amounts: Vec<f64> = transactions.iter()
        .filter(|t| t.approved)
        .map(|t| t.amount)
        .collect();
    approved_amounts.sort_by(|a, b| b.partial_cmp(a).unwrap()); // по убыванию
    let top3: Vec<f64> = approved_amounts.into_iter().take(3).collect();
    println!("  топ-3 суммы:        {:?}", top3);

    // Задача 3: количество отклонённых транзакций по категориям
    // (через fold — строим HashMap вручную без использования HashMap напрямую)
    let declined: Vec<&str> = transactions.iter()
        .filter(|t| !t.approved)
        .map(|t| t.category)
        .collect();
    println!("  отклонённые категории: {:?}", declined);

    // Задача 4: фабрика фильтров — создаём замыкание-фильтр динамически
    fn make_amount_filter(min: f64, max: f64) -> impl Fn(&Transaction) -> bool {
        move |t| t.amount >= min && t.amount <= max
    }

    let mid_range_filter = make_amount_filter(100.0, 1000.0);
    let mid_range: Vec<f64> = transactions.iter()
        .filter(|t| t.approved)
        .filter(|t| mid_range_filter(t))
        .map(|t| t.amount)
        .collect();
    println!("  одобрены от 100 до 1000: {:?}", mid_range);
}

