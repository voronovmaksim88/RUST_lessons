// ============================================================================
// Fn / FnMut / FnOnce:
//   ✓ Fn      — только читает захваченное; вызов возможен много раз
//   ✓ FnMut   — изменяет захваченное; вызов возможен много раз
//   ✓ FnOnce  — потребляет захваченное; вызов ровно один раз
//   ✓ Иерархия: Fn ⊂ FnMut ⊂ FnOnce (любое Fn реализует и FnMut, и FnOnce)
//
// Передача замыкания в функцию:
//   ✓ impl Fn / generics  → статическая диспетчеризация, нулевые накладные расходы
//   ✓ Box<dyn Fn>         → динамическая диспетчеризация, нужна когда тип неизвестен
//
// Возврат замыкания:
//   ✓ impl Fn    — один тип; move нужен чтобы захваченное не стало висячей ссылкой
//   ✓ Box<dyn Fn> — разные типы из разных веток
//
// Замыкания + итераторы:
//   ✓ map, filter, fold, take, skip, zip, flat_map, enumerate, filter_map
//   ✓ Адаптеры ленивы — работа выполняется только при вызове потребителя
//   ✓ Цепочки компилируются в эффективный код (zero-cost abstraction)
// ============================================================================
