// ============================================================================
// Урок 011. Match - сопоставление с образцом.
// ============================================================================
// Match - это мощный оператор сопоставления с образцом в Rust.
// Он похож на switch в других языках, но намного мощнее!
// ============================================================================

fn main() {
    println!("=== Урок 011. MATCH - СОПОСТАВЛЕНИЕ С ОБРАЗЦОМ ===\n");

    // ---------------------------------------------------------------------------
    // 1. БАЗОВОЕ ИСПОЛЬЗОВАНИЕ
    // ---------------------------------------------------------------------------
    println!("1. Базовое сопоставление:");
    let num = 2;
    match num {
        1 => println!("   один"),
        2 => println!("   два"),
        3 => println!("   три"),
        _ => println!("   другое число"),
    }

    // ---------------------------------------------------------------------------
    // 2. MATCH С ВОЗВРАТОМ ЗНАЧЕНИЯ
    // ---------------------------------------------------------------------------
    println!("\n2. Match возвращает значение:");
    let number = 5;
    let description = match number {
        1 => "единица",
        2 | 3 | 5 | 7 => "простое число (до 10)",
        4 | 6 | 8 | 10 => "чётное составное",
        _ => "другое",
    };
    println!("   Число {} - это {}", number, description);

    // ---------------------------------------------------------------------------
    // 3. ДИАПАЗОНЫ
    // ---------------------------------------------------------------------------
    println!("\n3. Использование диапазонов:");
    let age: u8 = 25;
    match age {
        0..=12 => println!("   Ребёнок (возраст: {})", age),
        13..=19 => println!("   Подросток (возраст: {})", age),
        20..=59 => println!("   Взрослый (возраст: {})", age),
        _ => println!("   Пожилой человек (возраст: {})", age),
    }

    // ---------------------------------------------------------------------------
    // 4. MATCH С КОРТЕЖАМИ
    // ---------------------------------------------------------------------------
    println!("\n4. Match с кортежами:");

    let user = ("Bob", 37);
    match user {
        ("Tom", 36) => println!("   Пользователь Tom, 36 лет"),
        (name, 37) => println!("   Пользователь с возрастом 37: {}", name),
        (name, age) => println!("   Пользователь {}: {} лет", name, age),
    }

    // ---------------------------------------------------------------------------
    // 5. MATCH С МАССИВАМИ
    // ---------------------------------------------------------------------------
    println!("\n5. Match с массивами:");

    let numbers = [2, 3, 4, 5, 6, 7, 8];
    match numbers {
        [1, 2, 3, ..] => println!("   Массив начинается с [1, 2, 3]"),
        [first, .., last] => println!("   Первый элемент: {}, последний элемент: {}", first, last),
    }

    let three_numbers = [1, 7, 3];
    match three_numbers {
        [1, 2, 3] => println!("   Точно [1, 2, 3]"),
        [x, 2, 3] => println!("   Первый элемент любой: [{}, 2, 3]", x),
        [x, y, 3] => println!("   Последний элемент равен 3: [{}, {}, 3]", x, y),
        [x, y, z] => println!("   Любой массив из трёх элементов: [{}, {}, {}]", x, y, z),
    }

    // ---------------------------------------------------------------------------
    // 6. MATCH С ENUM
    // ---------------------------------------------------------------------------
    println!("\n6. Match с enum:");

    #[derive(Debug)] // Автоматическая реализация трейта Debug для вывода
    #[allow(dead_code)] // Разрешаем неиспользуемые варианты (для учебных целей)
    enum Direction {
        North,
        South,
        East,
        West,
    }

    let direction = Direction::North;
    match direction {
        Direction::North => println!("   Идём на север!"),
        Direction::South => println!("   Идём на юг!"),
        Direction::East => println!("   Идём на восток!"),
        Direction::West => println!("   Идём на запад!"),
    }

    // ---------------------------------------------------------------------------
    // 7. ENUM С ДАННЫМИ
    // ---------------------------------------------------------------------------
    println!("\n7. Enum с данными:");

    #[allow(dead_code)]
    enum Message {
        Quit,
        Move { x: i32, y: i32 },
        Write(String),
        ChangeColor(i32, i32, i32),
    }

    let msg = Message::Move { x: 10, y: 20 };

    match msg {
        Message::Quit => println!("   Выход из программы"),
        Message::Move { x, y } => println!("   Перемещение в координаты x={}, y={}", x, y),
        Message::Write(text) => println!("   Запись текста: {}", text),
        Message::ChangeColor(r, g, b) => println!("   Смена цвета на RGB({}, {}, {})", r, g, b),
    }

    // ---------------------------------------------------------------------------
    // 8. MATCH С OPTION
    // Option<T> - это специальный enum в Rust для представления опциональных значений (значений,
    // которые могут существовать или отсутствовать).
    // Это альтернатива null/nil из других языков, но безопасная на уровне типов.
    // ---------------------------------------------------------------------------
    println!("\n8. Match с Option:");

    let some_number: Option<i32> = Some(42);
    let no_number: Option<i32> = None;

    match some_number {
        Some(n) => println!("   Получено число: {}", n),
        None => println!("   Ничего не получено"),
    }

    match no_number {
        Some(n) => println!("   Получено число: {}", n),
        None => println!("   Ничего не получено"),
    }

    // ---------------------------------------------------------------------------
    // 9. MATCH С УСЛОВИЯМИ (GUARDS)
    // ---------------------------------------------------------------------------
    println!("\n9. Match с условиями (guards):");

    let pair = (5, -5); // это кортеж
    match pair {
        (x, y) if x == y => println!("   Числа равны: x = y = {}", x),
        (x, y) if x + y == 0 => println!("   Числа противоположны: {} и {}", x, y),
        (x, y) if x > y => println!("   {} больше чем {}", x, y),
        (x, y) => println!("   Просто пара чисел: ({}, {})", x, y),
    }

    // ---------------------------------------------------------------------------
    // 10. MATCH С RESULT
    // Result<T, E> - это специальный enum в Rust для представления результатов операций,
    // которые могут завершиться успешно или с ошибкой.
    // Это альтернатива Exception из других языков, но безопасная на уровне типов.
    // ---------------------------------------------------------------------------
    println!("\n10. Match с Result:");

    let result: Result<i32, &str> = Ok(200);

    match result {
        Ok(code) => println!("   Успех! Код: {}", code),
        Err(e) => println!("   Ошибка: {}", e),
    }

    // ---------------------------------------------------------------------------
    // 11. СТРУКТУРЫ
    // ---------------------------------------------------------------------------
    println!("\n11. Сопоставление структур:");

    struct Point {
        x: i32,
        y: i32,
    }

    let point = Point { x: 0, y: 7 };

    match point {
        Point { x: 0, y: 0 } => println!("   Начало координат"),
        Point { x: 0, y } => println!("   На оси Y, координата y = {}", y),
        Point { x, y: 0 } => println!("   На оси X, координата x = {}", x),
        Point { x, y } => println!("   Произвольная точка ({}, {})", x, y),
    }

    // ---------------------------------------------------------------------------
    // 12. ИГНОРИРОВАНИЕ ЗНАЧЕНИЙ: _ И ..
    // ---------------------------------------------------------------------------
    println!("\n12. Игнорирование значений:");

    #[allow(dead_code)]
    struct Person {
        name: String,
        age: u8,
        height: f32,
    }

    let bob = Person {
        name: String::from("Bob"),
        age: 33,
        height: 1.70,
    };

    match bob {
        Person {
            name,
            age,
            height: _,
        } => {
            println!("   Имя: {}, возраст: {}", name, age);
        }
    }

    let tom = Person {
        name: String::from("Tom"),
        age: 22,
        height: 1.82,
    };

    match tom {
        Person { name, .. } => println!("   Только имя: {}", name),
    }

    let pair = ("Alice", 30);
    match pair {
        ("Bob", age) => println!("   Bob, возраст: {}", age),
        (name, _) => println!("   Возраст не нужен, имя: {}", name),
    }

    // ---------------------------------------------------------------------------
    // 13. ПЕРЕМЕННАЯ И ПАТТЕРН ОДНОВРЕМЕННО: @
    // ---------------------------------------------------------------------------
    println!("\n13. Связывание через @:");

    let id = 7;
    match id {
        user_id @ 1..=10 => println!("   ID в допустимом диапазоне: {}", user_id),
        _ => println!("   ID вне допустимого диапазона"),
    }

    // ---------------------------------------------------------------------------
    // 14. ВАЖНЫЙ НЮАНС: ПЕРЕМЕННЫЕ В ПАТТЕРНАХ
    // ---------------------------------------------------------------------------
    println!("\n14. Переменные в паттернах:");

    let expected_name = String::from("Bob");
    let person = Person {
        name: String::from("Bob"),
        age: 41,
        height: 1.70,
    };

    match person {
        // name здесь получает значение поля. Сравнение с expected_name делаем в guard.
        Person { name, age, .. } if name == expected_name && age == 41 => {
            println!("   Это Bob, 41 год");
        }
        Person { name, age, .. } => println!("   Другой человек: {}, {}", name, age),
    }

    // ---------------------------------------------------------------------------
    // 15. ПРАКТИЧЕСКИЙ ПРИМЕР: КАЛЬКУЛЯТОР
    // ---------------------------------------------------------------------------
    println!("\n15. Практический пример - калькулятор:");

    #[allow(dead_code)]
    enum Operation {
        Add(i32, i32),
        Subtract(i32, i32),
        Multiply(i32, i32),
        Divide(i32, i32),
    }

    let operation = Operation::Multiply(6, 7);

    let result = match operation {
        Operation::Add(a, b) => {
            println!("   Складываем {} + {}", a, b);
            a + b
        }
        Operation::Subtract(a, b) => {
            println!("   Вычитаем {} - {}", a, b);
            a - b
        }
        Operation::Multiply(a, b) => {
            println!("   Умножаем {} * {}", a, b);
            a * b
        }
        Operation::Divide(a, b) if b != 0 => {
            println!("   Делим {} / {}", a, b);
            a / b
        }
        Operation::Divide(a, b) => {
            println!("   Попытка деления {} на {}", a, b);
            println!("   Деление на ноль невозможно!");
            0
        }
    };
    println!("   Результат: {}", result);

    println!("\n✅ Урок по сопоставлению с образцом (match) завершён!");
}

// ============================================================================
// КЛЮЧЕВЫЕ МОМЕНТЫ:
// ============================================================================
// ✓ Match должен быть исчерпывающим (покрывать все случаи)
// ✓ _ используется как "catch-all" (поймать всё остальное)
// ✓ Match возвращает значение
// ✓ | позволяет объединять несколько паттернов
// ✓ if guards добавляют дополнительные условия
// ✓ @ позволяет одновременно проверить паттерн и сохранить значение в переменную
// ✓ _ и .. помогают игнорировать ненужные значения
// ============================================================================
