// ============================================================================
// Урок 012. ЦИКЛЫ В RUST
// ============================================================================
// В Rust существует 3 основных типа циклов:
// 1. loop    - бесконечный цикл (выход через break)
// 2. while   - цикл с условием
// 3. for     - итераторный цикл
//
// Смежные темы вынесены в отдельные уроки:
// - метки циклов и break со значением (подробно) -> урок 017
// - while let и другие циклы с паттернами        -> урок 031
// - адаптеры итераторов (map, filter, fold...)   -> уроки 050-053
// - итерация по HashMap                          -> урок 046
// - итерация по строкам (chars, bytes)           -> урок 018
// ============================================================================

fn main() {
    println!("╔════════════════════════════════════════════════════════════════╗");
    println!("║              Урок 012. ЦИКЛЫ В RUST                          ║");
    println!("╚════════════════════════════════════════════════════════════════╝\n");

    // ========================================================================
    // 1. ЦИКЛ LOOP - БЕСКОНЕЧНЫЙ ЦИКЛ
    // ========================================================================
    // loop - это бесконечный цикл, который выполняется, пока не встретит break
    // Используется когда количество итераций заранее неизвестно

    println!("╔═══ 1.1 Простой loop с break ═══╗");
    let mut n = 1;
    loop {
        println!("  n = {}", n);
        n += 1;
        if n == 6 {
            break; // Выход из цикла
        }
    }
    println!("  Цикл завершён\n");

    // ========================================================================
    // 2. LOOP С ВОЗВРАТОМ ЗНАЧЕНИЯ
    // ========================================================================
    // loop может возвращать значение через break
    // Это уникальная особенность Rust!

    println!("╔═══ 1.2 Loop с возвратом значения ═══╗");
    let mut m = 1;
    let result = loop {
        println!("  m = {}", m);
        m += 1;
        if m == 6 {
            break m * 2; // Возвращаем значение m * 2
        }
    };
    println!("  Результат цикла: {}\n", result);

    // ========================================================================
    // 3. LOOP С CONTINUE
    // ========================================================================
    // continue пропускает текущую итерацию и переходит к следующей

    println!("╔═══ 1.3 Loop с continue (пропуск чётных) ═══╗");
    let mut counter = 0;
    loop {
        counter += 1;

        if counter > 10 {
            break; // Выходим при counter > 10
        }

        if counter % 2 == 0 {
            continue; // Пропускаем чётные числа
        }

        println!("  Нечётное число: {}", counter);
    }
    println!();

    // ========================================================================
    // 4. ЦИКЛ WHILE - ЦИКЛ С УСЛОВИЕМ
    // ========================================================================
    // while выполняется пока условие истинно
    // Проверка условия происходит ПЕРЕД каждой итерацией

    println!("╔═══ 2.1 Простой while ═══╗");
    let mut count = 1;
    while count < 6 {
        println!("  count = {}", count);
        count += 1;
    }
    println!();

    println!("╔═══ 2.2 While с несколькими условиями ═══╗");
    let mut x = 0;
    let mut y = 10;
    while x < 5 && y > 5 {
        println!("  x = {}, y = {}", x, y);
        x += 1;
        y -= 1;
    }
    println!();

    // Есть ещё одна форма цикла — while let: она продолжает итерации,
    // пока значение совпадает с паттерном. Подробно — в уроке 031.

    // ========================================================================
    // 5. ЦИКЛ FOR - ИТЕРАТОРНЫЙ ЦИКЛ
    // ========================================================================
    // for - самый безопасный и часто используемый цикл в Rust
    // Используется для итерации по диапазонам и коллекциям

    println!("╔═══ 3.1 For с диапазоном (range) ═══╗");
    println!("  Диапазон 1..6 (исключает 6):");
    for num in 1..6 {
        println!("    num = {}", num);
    }

    println!("  Диапазон 1..=5 (включает 5):");
    for num in 1..=5 {
        println!("    num = {}", num);
    }
    println!();

    println!("╔═══ 3.2 For в обратном порядке ═══╗");
    for num in (1..=5).rev() {
        println!("  Обратный отсчёт: {}", num);
    }
    println!("  Пуск! 🚀\n");

    println!("╔═══ 3.3 For с шагом (step) ═══╗");
    println!("  Чётные числа от 0 до 10:");
    for num in (0..=10).step_by(2) {
        println!("    {}", num);
    }
    println!();

    println!("╔═══ 3.4 For с массивом ═══╗");
    let array = [10, 20, 30, 40, 50];
    println!("  Массив: {:?}", array);
    for element in array {
        println!("    Элемент: {}", element);
    }
    println!();

    // ========================================================================
    // 6. FOR С ENUMERATE - ИНДЕКС И ЗНАЧЕНИЕ
    // ========================================================================
    // Иногда в цикле нужен не только элемент, но и его номер.
    // Для этого используют цепочку .iter().enumerate():
    //
    // У массива нельзя вызвать .enumerate() напрямую — сначала нужен итератор.
    // fruits.iter()  — создаёт итератор по ссылкам на элементы (подробно: урок 051)
    // .enumerate()   — оборачивает его, добавляя к каждому элементу счётчик
    //
    // В итоге каждая итерация выдаёт кортеж (индекс, элемент),
    // который мы распаковываем прямо в for: for (index, fruit) in ...
    // Индексы всегда начинаются с 0.

    println!("╔═══ 3.5 For с enumerate (индекс + значение) ═══╗");
    let fruits = ["Яблоко", "Банан", "Апельсин", "Манго"];
    for (index, fruit) in fruits.iter().enumerate() {
        println!("  Фрукт #{}: {}", index + 1, fruit);
    }
    println!();

    // ========================================================================
    // 7. ТРИ ФОРМЫ FOR ПО КОЛЛЕКЦИИ
    // ========================================================================
    // В Rust есть 3 способа пройтись по коллекции:
    // - for x in &coll     - по ссылкам (коллекция остаётся доступной)
    // - for x in &mut coll - по изменяемым ссылкам (можно менять элементы)
    // - for x in coll      - по значениям
    //
    // За этими формами стоят методы iter() / iter_mut() / into_iter()
    // и правила владения — подробный разбор в уроке 051.

    println!("╔═══ 3.6 Три формы for ═══╗");

    let numbers = [1, 2, 3, 4, 5];
    print!("  for x in &numbers:     ");
    for num in &numbers {
        print!("{} ", num);
    }
    println!("(numbers доступен: {:?})", numbers);

    let mut doubled = [1, 2, 3, 4, 5];
    for num in &mut doubled {
        *num *= 2; // изменяем элементы через изменяемую ссылку
    }
    println!("  for x in &mut doubled: элементы удвоены: {:?}", doubled);

    print!("  for x in numbers:      ");
    // Форма без & забирает коллекцию ПО ЗНАЧЕНИЮ (потребляющая форма).
    // Здесь numbers останется доступен, но только потому, что [i32; 5]
    // реализует Copy — в цикл уходит КОПИЯ массива.
    // Vec<T> или массив со String были бы ПЕРЕМЕЩЕНЫ в цикл
    // и после него стали бы недоступны. Подробно — в уроке 051.
    for num in numbers {
        print!("{} ", num);
    }
    println!("(по значениям)");
    println!("  numbers после цикла доступен (массив Copy): {:?}\n", numbers);

    // ========================================================================
    // 8. ВЛОЖЕННЫЕ ЦИКЛЫ
    // ========================================================================

    println!("╔═══ 4.1 Вложенные циклы - таблица умножения ═══╗");
    println!("  Таблица умножения 5x5:");
    print!("     ");
    for i in 1..=5 {
        print!("{:4}", i);
    }
    println!();
    print!("    ");
    for _ in 1..=5 {
        print!("────");
    }
    println!();

    for i in 1..=5 {
        print!("{:2} │", i);
        for j in 1..=5 {
            print!("{:4}", i * j);
        }
        println!();
    }
    println!();

    // ========================================================================
    // 9. МЕТКИ ЦИКЛОВ (КРАТКО)
    // ========================================================================
    // Обычный break выходит только из ВНУТРЕННЕГО цикла.
    // Метка ('имя: перед циклом) позволяет управлять внешним циклом:
    //   break 'имя    - выход из помеченного цикла
    //   continue 'имя - следующая итерация помеченного цикла
    // Подробный разбор меток и break со значением — в уроке 017.

    println!("╔═══ 4.2 Метки циклов (кратко) ═══╗");
    'outer: for i in 1..=4 {
        for j in 1..=4 {
            if i * j == 6 {
                println!("  Нашли пару: i={}, j={} (i*j=6)", i, j);
                break 'outer; // выходим сразу из ОБОИХ циклов
            }
        }
    }
    println!("  Подробно о метках — урок 017\n");

    // ========================================================================
    // 10. ПРАКТИЧЕСКИЕ ПРИМЕРЫ
    // ========================================================================

    println!("╔═══ 5.1 Нахождение простых чисел ═══╗");
    let limit = 30;
    println!("  Простые числа до {}:", limit);
    print!("  ");

    'outer_prime: for num in 2..=limit {
        for divisor in 2..num {
            if num % divisor == 0 {
                continue 'outer_prime; // Не простое число
            }
        }
        print!("{} ", num);
    }
    println!("\n");

    println!("╔═══ 5.2 Факториал с циклом ═══╗");
    let n = 5;
    let mut factorial = 1u64;
    for i in 1..=n {
        factorial *= i;
    }
    println!("  {}! = {}\n", n, factorial);

    println!("╔═══ 5.3 Числа Фибоначчи ═══╗");
    let fib_count = 10;
    let (mut a, mut b) = (0u64, 1u64);
    println!("  Первые {} чисел Фибоначчи:", fib_count);
    print!("  ");

    for _ in 0..fib_count {
        print!("{} ", a);
        let temp = a + b;
        a = b;
        b = temp;
    }
    println!("\n");

    println!("╔═══ 5.4 Поиск в массиве ═══╗");
    let names = ["Анна", "Борис", "Вера", "Григорий", "Дарья"];
    let search_name = "Вера";
    let mut found = false;

    // iter().enumerate() из §3.5: на каждой итерации — кортеж (индекс, элемент)
    for (index, name) in names.iter().enumerate() {
        if *name == search_name {
            println!("  Найдено: '{}' на позиции {}", name, index);
            found = true;
            break;
        }
    }

    if !found {
        println!("  '{}' не найдено", search_name);
    }
    println!();

    println!("╔═══ 5.5 Обработка с match внутри цикла ═══╗");
    // Option и match знакомы из урока 011
    let results = [Some(10), None, Some(20), Some(30), None, Some(40)];
    let mut sum = 0;

    for result in results {
        match result {
            Some(value) => {
                println!("  Найдено значение: {}", value);
                sum += value;
            }
            None => {
                println!("  Пропущено: None");
            }
        }
    }
    println!("  Сумма всех значений: {}\n", sum);

    // ========================================================================
    // 11. БЕСКОНЕЧНЫЕ ЦИКЛЫ И УСЛОВИЯ ВЫХОДА
    // ========================================================================

    println!("╔═══ 5.6 Симуляция игрового цикла ═══╗");
    let mut health = 100;
    let mut turn = 0;

    loop {
        turn += 1;
        health -= 15;

        println!("  Ход {}: Здоровье = {}", turn, health);

        if health <= 0 {
            println!("  Игра окончена! Продержались {} ходов", turn);
            break;
        }

        if turn >= 8 {
            println!("  Достигнут лимит ходов!");
            break;
        }
    }
    println!();

    // ========================================================================
    // ЧТО ДАЛЬШЕ
    // ========================================================================
    // Темы, тесно связанные с циклами, разобраны в отдельных уроках:
    // - урок 017: метки циклов и возврат значения из break (подробно)
    // - урок 031: while let, if let, let...else
    // - уроки 050-053: итераторы и их адаптеры (map, filter, fold, zip...)
    // - урок 046: итерация по HashMap
    // - урок 018: итерация по строкам (chars, bytes, lines)

    // ========================================================================
    // РЕЗЮМЕ
    // ========================================================================

    println!("╔════════════════════════════════════════════════════════════════╗");
    println!("║                         РЕЗЮМЕ                                 ║");
    println!("╠════════════════════════════════════════════════════════════════╣");
    println!("║ • loop       - бесконечный цикл, выход через break             ║");
    println!("║ • while      - цикл с условием                                 ║");
    println!("║ • for        - итерация по диапазонам и коллекциям             ║");
    println!("║ • break      - выход из цикла (может вернуть значение в loop)  ║");
    println!("║ • continue   - переход к следующей итерации                    ║");
    println!("║ • 'label:    - метки для вложенных циклов (подробно: урок 017) ║");
    println!("╠════════════════════════════════════════════════════════════════╣");
    println!("║ Три формы for по коллекции (подробно: урок 051):                ║");
    println!("║ • for x in &coll     - по ссылкам                              ║");
    println!("║ • for x in &mut coll - по изменяемым ссылкам                   ║");
    println!("║ • for x in coll      - по значениям                            ║");
    println!("╠════════════════════════════════════════════════════════════════╣");
    println!("║ Диапазоны: 1..6 (без конца), 1..=5 (с концом),                  ║");
    println!("║ (..).rev() - обратный порядок, .step_by(n) - с шагом           ║");
    println!("╚════════════════════════════════════════════════════════════════╝");

    println!("\n✅ Урок по циклам в Rust завершён!");
}
