//! Целые числа со знаком (signed integers).
//!
//! Типы: `i8`, `i16`, `i32`, `i64`, `i128`.
//! Могут хранить как положительные, так и отрицательные значения.
//! Размер в битах указан в конце имени.
//!
//! | Тип | Размер | Диапазон |
//! |-----|--------|----------|
//! | `i8` | 1 байт | −128 … 127 |
//! | `i16` | 2 байта | −32 768 … 32 767 |
//! | `i32` | 4 байта | −2³¹ … 2³¹−1 |
//! | `i64` | 8 байт | −9.2·10¹⁸ … 9.2·10¹⁸ |
//! | `i128` | 16 байт | ~ ±1.7·10³⁸ |
//!
//! Тип по умолчанию для целых чисел — `i32`.

// ========================================================================
// ЦЕЛЫЕ ЧИСЛА СО ЗНАКОМ (signed integers)
// ========================================================================
// Могут хранить как положительные, так и отрицательные значения.
// Формат: i + количество бит (i8, i16, i32, i64, i128)

pub fn show() {
    println!("╔════════════════════════════════════════════════════════════════╗");
    println!("║          ЦЕЛЫЕ ЧИСЛА СО ЗНАКОМ (signed integers)               ║");
    println!("╚════════════════════════════════════════════════════════════════╝\n");

    // i8: 8 бит, диапазон от -128 до 127
    let my_var_i8: i8 = 127;
    let my_var_i8_min: i8 = -128;
    println!("i8: от {} до {}", my_var_i8_min, my_var_i8);
    println!("  Размер: {} байт", std::mem::size_of::<i8>());

    // i16: 16 бит, диапазон от -32 768 до 32 767
    let my_var_i16: i16 = 32_767; // подчёркивания для читаемости
    println!("\ni16 max: {}", my_var_i16);
    println!("  Размер: {} байт", std::mem::size_of::<i16>());

    // i32: 32 бита, диапазон от -2 147 483 648 до 2 147 483 647
    // Это тип по умолчанию для целых чисел!
    let my_var_i32: i32 = 2_147_483_647;
    let default_int = 42; // Rust автоматически выберет i32
    println!("\ni32 max: {}, default: {}", my_var_i32, default_int);
    println!("  Размер: {} байт", std::mem::size_of::<i32>());
    println!("  ⭐ Это тип по умолчанию для целых чисел!");

    // i64: 64 бита, диапазон от -9 223 372 036 854 775 808 до 9 223 372 036 854 775 807
    let my_var_i64: i64 = 9_223_372_036_854_775_807;
    println!("\ni64 max: {}", my_var_i64);
    println!("  Размер: {} байт", std::mem::size_of::<i64>());

    // i128: 128 бит, очень большие числа (примерно ±1.7 × 10^38)
    let my_var_i128: i128 = 170_141_183_460_469_231_731_687_303_715_884_105_727;
    println!("\ni128 max: {}", my_var_i128);
    println!("  Размер: {} байт", std::mem::size_of::<i128>());

    println!("\n📝 Совет: Используйте подчёркивания для читаемости больших чисел!");
    println!("   Например: 1_000_000 вместо 1000000");
}
