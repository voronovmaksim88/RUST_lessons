//! Изменяемость (`mut`) и затенение (`shadowing`).
//!
//! ## Неизменяемость (immutability)
//!
//! По умолчанию все переменные в Rust **неизменяемые**. Это повышает
//! безопасность и позволяет компилятору делать оптимизации.
//!
//! ## Изменяемость (`mut`)
//!
//! Ключевое слово `mut` разрешает изменение **существующей** переменной.
//! Тип при этом остаётся тем же.
//!
//! ## Затенение (shadowing)
//!
//! Повторное объявление `let` с тем же именем **создаёт новую переменную**,
//! которая «затеняет» предыдущую. Можно менять тип!
//!
//! | Механизм | Меняет тип | Создаёт новую переменную |
//! |----------|-----------|--------------------------|
//! | `mut` | ❌ | ❌ |
//! | shadowing | ✅ | ✅ |

// ========================================================================
// ИЗМЕНЯЕМОСТЬ И ЗАТЕНЕНИЕ (mutability and shadowing)
// ========================================================================
// По умолчанию все переменные в Rust неизменяемые (immutable)
// Это помогает избежать ошибок и делает код более предсказуемым

pub fn show() {
    println!("\n╔════════════════════════════════════════════════════════════════╗");
    println!("║          ИЗМЕНЯЕМОСТЬ И ЗАТЕНЕНИЕ (mutability)            ║");
    println!("╚════════════════════════════════════════════════════════════════╝\n");

    // ═══════════════════════════════════════════════════════════════════
    // НЕИЗМЕНЯЕМЫЕ ПЕРЕМЕННЫЕ (immutable)
    // ═══════════════════════════════════════════════════════════════════
    println!("═══ НЕИЗМЕНЯЕМЫЕ ПЕРЕМЕННЫЕ (по умолчанию) ═══\n");

    let immutable_var = 10;
    println!("  let immutable_var = 10;");
    println!("  Значение: {}", immutable_var);
    println!("  ✓ Безопасно от случайных изменений");
    println!("  ✓ Может быть оптимизировано компилятором");

    // Попытка изменить вызовет ошибку компиляции:
    // immutable_var = 20; // ❌ ОШИБКА! cannot assign twice to immutable variable

    println!("\n  ⚠️ Попытка изменить:");
    println!("     immutable_var = 20; // ОШИБКА компиляции!");
    println!("     cannot assign twice to immutable variable");

    // ═══════════════════════════════════════════════════════════════════
    // ИЗМЕНЯЕМЫЕ ПЕРЕМЕННЫЕ (mutable)
    // ═══════════════════════════════════════════════════════════════════
    println!("\n═══ ИЗМЕНЯЕМЫЕ ПЕРЕМЕННЫЕ (mut) ═══\n");

    let mut mutable_var: u32 = 22;
    println!("  let mut mutable_var: u32 = 22;");
    println!("  До изменения: {}", mutable_var);

    mutable_var = mutable_var + 1;
    println!("  После +1: {}", mutable_var);

    mutable_var += 10; // Сокращённая запись
    println!("  После +=10: {}", mutable_var);

    mutable_var -= 3;
    println!("  После -=3: {}", mutable_var);

    mutable_var *= 2;
    println!("  После *=2: {}", mutable_var);

    mutable_var /= 3;
    println!("  После /=3: {}", mutable_var);

    println!("\n  💡 Ключевое слово 'mut' явно указывает на намерение изменять");

    // Примеры с различными типами
    println!("\n═══ Примеры изменяемых переменных ═══");

    let mut counter = 0;
    println!("\n  Счётчик:");
    for i in 1..=5 {
        counter += i;
        println!("    Итерация {}: counter = {}", i, counter);
    }

    let mut flag = true;
    println!("\n  Флаг:");
    println!("    До: {}", flag);
    flag = !flag;
    println!("    После инверсии: {}", flag);

    let mut text = String::from("Hello");
    println!("\n  Строка:");
    println!("    До: \"{}\"", text);
    text.push_str(", World!");
    println!("    После: \"{}\"", text);

    // ═══════════════════════════════════════════════════════════════════
    // ЗАТЕНЕНИЕ ПЕРЕМЕННЫХ (shadowing)
    // ═══════════════════════════════════════════════════════════════════
    println!("\n\n═══ ЗАТЕНЕНИЕ ПЕРЕМЕННЫХ (shadowing) ═══\n");

    println!("  Можно объявить новую переменную с тем же именем:");
    println!("  Новая переменная \"затеняет\" предыдущую\n");

    let shadow = 5;
    println!("  let shadow = 5;");
    println!("    shadow = {}", shadow);

    let shadow = shadow + 10; // Затеняем, создавая новую переменную
    println!("\n  let shadow = shadow + 10;");
    println!("    shadow = {} (новая переменная!)", shadow);

    let shadow = shadow * 2;
    println!("\n  let shadow = shadow * 2;");
    println!("    shadow = {} (ещё одна новая переменная!)", shadow);

    // При затенении можно даже изменить тип!
    println!("\n═══ Изменение типа при затенении ═══");

    let spaces = "   "; // &str
    println!("  let spaces = \"   \"; // тип: &str");
    println!("    Значение: \"{}\" (строка из 3 пробелов)", spaces);

    let spaces = spaces.len(); // usize
    println!("\n  let spaces = spaces.len(); // тип: usize");
    println!("    Значение: {} (число)", spaces);
    println!("    ✓ Тип изменился с &str на usize!");

    // Ещё примеры затенения с изменением типа
    println!("\n═══ Больше примеров затенения ═══");

    let value = "42";
    println!("  let value = \"42\"; // тип: &str");
    println!("    value = \"{}\"", value);

    let value: i32 = value.parse().unwrap();
    println!("\n  let value: i32 = value.parse().unwrap();");
    println!("    value = {} (теперь число!)", value);

    let value = value * 2;
    println!("\n  let value = value * 2;");
    println!("    value = {}", value);

    // ═══════════════════════════════════════════════════════════════════
    // РАЗНИЦА МЕЖДУ mut И ЗАТЕНЕНИЕМ
    // ═══════════════════════════════════════════════════════════════════
    println!("\n\n═══ РАЗНИЦА МЕЖДУ mut И ЗАТЕНЕНИЕМ ═══\n");

    println!("  mut (изменяемость):");
    println!("    • Изменяет СУЩЕСТВУЮЩУЮ переменную");
    println!("    • Тип остаётся тем же");
    println!("    • Синтаксис: let mut x = 5; x = 10;");
    println!("    • Используйте для накопления/счётчиков\n");

    let mut x = 5;
    println!("    let mut x = 5;");
    println!("    Значение: x = {}", x);
    x = 10; // изменяем существующую
    println!("    x = 10; // изменение");
    println!("    Результат: x = {}", x);
    // x = "hello"; // ❌ ОШИБКА! нельзя изменить тип

    println!("\n  shadowing (затенение):");
    println!("    • Создаёт НОВУЮ переменную с тем же именем");
    println!("    • Можно изменить тип");
    println!("    • Синтаксис: let y = 5; let y = 10;");
    println!("    • Используйте для трансформации данных\n");

    let _y = 5;
    println!("    let y = 5;");
    let y = 10; // новая переменная
    println!("    let y = 10; // новая переменная");
    println!("    Результат: y = {}", y);

    let y = "hello"; // ✓ OK! можно изменить тип
    println!("    let y = \"hello\"; // можно изменить тип!");
    println!("    Результат: y = \"{}\"", y);

    // ═══════════════════════════════════════════════════════════════════
    // ПРАКТИЧЕСКИЕ ПРИМЕРЫ
    // ═══════════════════════════════════════════════════════════════════
    println!("\n═══ ПРАКТИЧЕСКИЕ ПРИМЕРЫ ═══");

    // Пример 1: Накопление суммы (mut)
    println!("\n  1. Накопление суммы (используем mut):");
    let numbers = [1, 2, 3, 4, 5];
    let mut sum = 0;
    for num in numbers.iter() {
        sum += num;
    }
    println!("     Сумма [1,2,3,4,5] = {}", sum);

    // Пример 2: Преобразование данных (shadowing)
    println!("\n  2. Преобразование данных (используем shadowing):");
    let input = "  12345  ";
    println!("     Ввод: \"{}\"", input);

    let input = input.trim(); // убираем пробелы
    println!("     После trim(): \"{}\"", input);

    let input: i32 = input.parse().unwrap(); // парсим в число
    println!("     После parse(): {}", input);

    let input = input * 2; // умножаем
    println!("     После *2: {}", input);

    // Пример 3: Изменяемые коллекции
    println!("\n  3. Изменяемые коллекции:");
    let mut vec = vec![1, 2, 3];
    println!("     До: {:?}", vec);
    vec.push(4);
    vec.push(5);
    println!("     После push: {:?}", vec);

    // Пример 4: Затенение в блоках
    println!("\n  4. Затенение в блоках:");
    let x = 10;
    println!("     Внешний x = {}", x);
    {
        let x = 20; // затенение внутри блока
        println!("     Внутренний x = {}", x);
    }
    println!("     Снова внешний x = {}", x);

    // ═══════════════════════════════════════════════════════════════════
    // КОГДА ЧТО ИСПОЛЬЗОВАТЬ
    // ═══════════════════════════════════════════════════════════════════
    println!("\n═══ КОГДА ЧТО ИСПОЛЬЗОВАТЬ ═══\n");

    println!("  Используйте НЕИЗМЕНЯЕМОСТЬ (по умолчанию) когда:");
    println!("    ✓ Значение не должно меняться");
    println!("    ✓ Нужна безопасность от ошибок");
    println!("    ✓ Хотите ясно показать намерения кода");
    println!("    ✓ Большинство случаев!\n");

    println!("  Используйте mut когда:");
    println!("    ✓ Накапливаете значение (счётчики, суммы)");
    println!("    ✓ Изменяете состояние объекта");
    println!("    ✓ Работаете с изменяемыми коллекциями");
    println!("    ✓ Нужна производительность (избегаете копирований)\n");

    println!("  Используйте shadowing когда:");
    println!("    ✓ Трансформируете данные через этапы");
    println!("    ✓ Нужно изменить тип данных");
    println!("    ✓ Хотите повторно использовать имя переменной");
    println!("    ✓ Парсинг и преобразование входных данных\n");

    println!("📝 Философия Rust:");
    println!("  • Неизменяемость по умолчанию = безопасность");
    println!("  • mut = явное указание на изменение");
    println!("  • Компилятор проверяет правильность использования");
    println!("  • Меньше ошибок, больше уверенности в коде!");
}
