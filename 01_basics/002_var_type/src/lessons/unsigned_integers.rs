//! Целые числа без знака (unsigned integers).
//!
//! Типы: `u8`, `u16`, `u32`, `u64`, `u128`.
//! Могут хранить только положительные значения и ноль.
//! Диапазон в два раза больше, чем у соответствующего знакового типа.
//!
//! | Тип | Размер | Диапазон |
//! |-----|--------|----------|
//! | `u8` | 1 байт | 0 … 255 |
//! | `u16` | 2 байта | 0 … 65 535 |
//! | `u32` | 4 байта | 0 … 4.29·10⁹ |
//! | `u64` | 8 байт | 0 … 1.84·10¹⁹ |
//! | `u128` | 16 байт | 0 … 3.4·10³⁸ |
//!
//! `u8` часто используется для работы с байтами и RGB-цветами.

// ========================================================================
// ЦЕЛЫЕ ЧИСЛА БЕЗ ЗНАКА (unsigned integers)
// ========================================================================
// Могут хранить только положительные значения (и ноль).
// Формат: u + количество бит (u8, u16, u32, u64, u128)

pub fn show() {
    println!("\n╔════════════════════════════════════════════════════════════════╗");
    println!("║         ЦЕЛЫЕ ЧИСЛА БЕЗ ЗНАКА (unsigned integers)         ║");
    println!("╚════════════════════════════════════════════════════════════════╝\n");

    // u8: 8 бит, диапазон от 0 до 255
    // Часто используется для работы с байтами и цветами (RGB)
    let my_var_u8: u8 = 255;
    println!("u8 max: {}", my_var_u8);
    println!("  Размер: {} байт", std::mem::size_of::<u8>());
    println!("  💡 Часто используется для RGB цветов и байтов");

    // u16: 16 бит, диапазон от 0 до 65 535
    let my_var_u16: u16 = 65_535;
    println!("\nu16 max: {}", my_var_u16);
    println!("  Размер: {} байт", std::mem::size_of::<u16>());

    // u32: 32 бита, диапазон от 0 до 4 294 967 295
    let my_var_u32: u32 = 4_294_967_295;
    println!("\nu32 max: {}", my_var_u32);
    println!("  Размер: {} байт", std::mem::size_of::<u32>());

    // u64: 64 бита, диапазон от 0 до 18 446 744 073 709 551 615
    let my_var_u64: u64 = 18_446_744_073_709_551_615;
    println!("\nu64 max: {}", my_var_u64);
    println!("  Размер: {} байт", std::mem::size_of::<u64>());

    // u128: 128 бит, диапазон от 0 до примерно 3.4 × 10^38
    let my_var_u128: u128 = 340_282_366_920_938_463_463_374_607_431_768_211_455;
    println!("\nu128 max: {}", my_var_u128);
    println!("  Размер: {} байт", std::mem::size_of::<u128>());

    println!("\n📝 Преимущество беззнаковых типов:");
    println!("   • Диапазон в два раза больше, чем у знаковых");
    println!("   • Идеальны для счётчиков, размеров, индексов");
    println!("   • Гарантия отсутствия отрицательных значений");
}
