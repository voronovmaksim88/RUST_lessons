//! Модуль захвата переменных в замыканиях.
//!
//! ## О чём этот модуль
//!
//! Главная фишка замыканий — они могут «видеть» переменные, которые объявлены
//! **снаружи** замыкания. Как именно замыкание получает доступ к чужой переменной?
//!
//! Есть три способа, от самого лёгкого к самому жёсткому:
//!
//! 1. **Прочитать** чужую переменную (захват по ссылке `&T`)
//! 2. **Изменить** чужую переменную (захват по изменяемой ссылке `&mut T`)
//! 3. **Забрать себе навсегда** (захват с `move` — владение переходит в замыкание)
//!
//! Эти три способа соответствуют трём трейтам: `Fn`, `FnMut`, `FnOnce`.

/// ## Захват по ссылке и изменяемой ссылке
///
/// Представьте, что переменная — это документ на столе.
/// Замыкание может **прочитать** документ (не забирая его) или **писать в него**
/// (не забирая, но блокируя доступ другим).
pub fn demonstrate_capture_by_ref() {
    println!("\n=== Пример 4: Захват переменных из окружения ===");

    // ----------------------------------------------------------------
    //  ЧАСТЬ 1. Прочитать — захват по обычной ссылке (&T)
    // ----------------------------------------------------------------
    // Замыкание видит greeting, но не трогает право владения.
    // Переменная как была у нас, так и остаётся.

    let greeting = String::from("Привет");

    let greet = || {
        // Мы ПРОСТО ЧИТАЕМ greeting. Замыкание автоматически берёт
        // ссылку &greeting — так же, как если бы мы написали &greeting вручную.
        // Никто ничего не забирает и не портит.
        println!("{}, пользователь!", greeting);
    };

    greet(); // Вызвали — прочитало

    // greeting всё ещё наша. Замыкание только посмотрело, не забрало.
    // Сравните с Примером 5 (move), где переменная уходит в замыкание навсегда.
    println!("Переменная greeting всё ещё доступна: {}", greeting);

    // ----------------------------------------------------------------
    //  ЧАСТЬ 2. Изменить — захват по изменяемой ссылке (&mut T)
    // ----------------------------------------------------------------
    // Теперь замыкание не просто читает, а МЕНЯЕТ переменную снаружи.
    // Для этого оно берёт &mut ссылку. Правило Rust: &mut ссылка может
    // быть только одна. Пока замыкание живо, никто другой не может даже
    // прочитать counter (даже мы сами!).

    let mut counter = 0; // mut обязателен — мы будем менять

    // Замыкание ДОЛЖНО быть let mut, потому что оно меняет своё
    // внутреннее состояние — хранит &mut counter.
    let mut increment = || {
        counter += 1; // Меняем чужую переменную через &mut ссылку
        println!("Счётчик: {}", counter);
    };

    // Каждый вызов увеличивает счётчик на 1.
    // Это работает, потому что замыкание хранит живую связь с counter,
    // а не копию числа.
    increment(); // counter стал 1
    increment(); // counter стал 2
    increment(); // counter стал 3

    // Как только мы перестали использовать замыкание increment,
    // Rust автоматически отпускает &mut ссылку на counter.
    // Теперь counter снова полностью наш, можем читать и менять.
    println!("Финальное значение счётчика: {}", counter);
}

/// ## Захват с `move` — переменная уходит в замыкание навсегда
pub fn demonstrate_move_closure() {
    println!("\n=== Пример 5: Принудительное перемещение (move) ===");

    let name = String::from("Алексей");

    // Ключевое слово move означает: «замыкание, забери всё, что тебе нужно,
    // с полным правом владения». Даже если замыкание всего лишь читает
    // переменную — она всё равно ПЕРЕНОСИТСЯ внутрь.
    //
    // Без move замыкание взяло бы просто &name (как в Части 1).
    // Но мы специально написали move — чтобы name ушёл в замыкание.
    let print_name = move || {
        // name теперь живёт внутри замыкания. Это полноценный String,
        // а не ссылка. Замыкание — единственный владелец.
        //
        // Когда замыкание завершится (выйдет из области видимости),
        // name будет автоматически удалён. Прямо как обычная переменная.
        println!("Имя: {}", name);
    };

    print_name(); // Первый вызов: читаем name внутри замыкания
    print_name(); // Второй вызов: снова читаем (name никуда не делся)

    // ВАЖНЫЙ МОМЕНТ: хотя move делает замыкание FnOnce (формально — «можно
    // вызвать только один раз»), на практике его МОЖНО вызывать многократно,
    // если внутри нет операции потребления (типа передачи name в другую
    // переменную или drop). Здесь мы только печатаем — ничего не потребляется.

    // А снаружи name уже недоступен — он переехал в замыкание.
    // Если раскомментировать следующую строку, будет ошибка компиляции:
    // println!("{}", name); // ОШИБКА: borrow of moved value: `name`
    println!("Переменная name была перемещена в замыкание и больше недоступна здесь");
}

/// ## Замыкание как аргумент функции
///
/// Замыкание можно передать в обычную функцию — например, чтобы та функция
/// применила замыкание к каким-то данным. Это как дать другу инструкцию:
/// «вот тебе числа, посчитай по этой формуле».
pub fn demonstrate_closures_as_args() {
    println!("\n=== Пример 6: Замыкания как аргументы функций ===");

    // Функция принимает два числа и замыкание-операцию.
    // Она ничего не знает, ЧТО делает замыкание — просто вызывает его.
    fn apply_operation<F>(a: i32, b: i32, operation: F) -> i32
    where
        // F: Fn(...) означает: «замыкание можно вызывать много раз,
        // и оно не портит свои переменные». Это самый мягкий вариант.
        // Если бы замыкание меняло переменную снаружи — нужен был бы FnMut.
        // Если бы забирало владение — FnOnce.
        F: Fn(i32, i32) -> i32,
    {
        operation(a, b) // Просто вызываем то, что нам передали
    }

    // Передаём разные замыкания — функция одна, а поведение разное
    let sum = apply_operation(10, 20, |x, y| x + y); // сложить
    let product = apply_operation(10, 20, |x, y| x * y); // умножить
    let max = apply_operation(10, 20, |x, y| {
        if x > y { x } else { y } // найти максимум
    });

    println!("Сумма: {}", sum);
    println!("Произведение: {}", product);
    println!("Максимум: {}", max);
}

/// Запускает все примеры захвата переменных.
pub fn run_all() {
    demonstrate_capture_by_ref();
    demonstrate_move_closure();
    demonstrate_closures_as_args();
}
