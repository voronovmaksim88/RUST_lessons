//! Модуль базового синтаксиса замыканий.
//!
//! Содержит примеры:
//! - замыканий без параметров
//! - замыканий с параметрами
//! - однострочных замыканий
//! - явного указания возвращаемого типа
//!
//! # Что такое замыкание?
//!
//! **Замыкание (closure)** — это анонимная функция, которую можно:
//! - сохранить в переменной
//! - передать как аргумент другой функции
//! - вернуть из функции
//! - «захватить» переменные из окружающей области видимости
//!
//! Главное отличие замыкания от обычной функции — способность **захватывать**
//! переменные из того места, где оно было создано. Это делает замыкания мощным
//! инструментом для функционального программирования и работы с итераторами.
//!
//! # Синтаксис замыканий
//!
//! Замыкание определяется с помощью **вертикальных линий** `|параметры|`:
//!
//! ```text
//! let имя_замыкания = |параметры| тело;
//! ```
//!
//! ## Компоненты замыкания:
//!
//! | Компонент | Описание | Пример |
//! |-----------|----------|--------|
//! | `let имя` | Переменная для хранения замыкания | `let f` |
//! | `\|\|` | Пустые параметры (нет аргументов) | `\|\| println!("Hi")` |
//! | `\|x\|` | Один параметр без типа (выводится автоматически) | `\|x\| x * 2` |
//! | `\|x: i32\|` | Параметр с явным типом | `\|x: i32\| x + 1` |
//! | `\|a, b\|` | Несколько параметров | `\|a, b\| a + b` |
//! | `\-> тип` | Возвращаемый тип (необязательно) | `\|x\| -> i32 { x }` |
//! | `{ ... }` | Тело с несколькими выражениями | `\|x\| { let y = x; y }` |
//! | выражение | Тело из одного выражения (возврат автоматический) | `\|x\| x * 2` |
//!
//! ## Формы замыканий:
//!
//! 1. **Без параметров:** `|| println!("Привет!")`
//! 2. **С параметрами:** `|x: i32| x * 2`
//! 3. **С телом-выражением:** `|x| x + 1` (возврат неявный)
//! 4. **С телом-блоком:** `|x| { let y = x + 1; y * 2 }`
//! 5. **С возвращаемым типом:** `|x| -> i32 { x }`
//!
//! ## Сравнение с функциями:
//!
//! | Характеристика | Функция | Замыкание |
//! |----------------|---------|-----------|
//! | Имя | Обязательное | Не требуется (анонимное) |
//! | Синтаксис | `fn имя() {}` | `\|\| {}` |
//! | Захват переменных | Нет | Да |
//! | Тип | Именованный тип | Анонимный тип (трейт Fn) |
//! | Параметры | Требуют типов | Типы можно не указывать |
//! | Возврат | Требуется `return` или последнее выражение | Последнее выражение |

/// Демонстрирует базовый синтаксис замыканий.
pub fn demonstrate_basic_syntax() {
    println!("\n=== Пример 1: Базовый синтаксис замыканий ===");

    // Замыкание без параметров
    let say_hello = || println!("Привет, мир!");
    say_hello();

    // Замыкание с одним параметром (тип указан явно)
    let square = |n: i32| {
        let result = n * n;
        println!("Квадрат числа {} равен {}", n, result);
    };
    square(4);
    square(5);

    // Замыкание с неявным типом параметра
    let double = |x| x * 2;
    println!("Удвоенное значение 10: {}", double(10));
}

/// Демонстрирует однострочные замыкания.
pub fn demonstrate_oneline_closures() {
    println!("\n=== Пример 2: Однострочные замыкания ===");

    let add = |a: i32, b: i32| a + b;
    let multiply = |a: i32, b: i32| a * b;
    let subtract = |a: i32, b: i32| a - b;

    println!("5 + 3 = {}", add(5, 3));
    println!("5 * 3 = {}", multiply(5, 3));
    println!("5 - 3 = {}", subtract(5, 3));
}

/// Демонстрирует замыкания с явным указанием возвращаемого типа.
pub fn demonstrate_explicit_return_type() {
    println!("\n=== Пример 3: Явное указание возвращаемого типа ===");

    let divide = |a: f64, b: f64| -> f64 {
        if b == 0.0 {
            println!("Ошибка: деление на ноль!");
            0.0
        } else {
            a / b
        }
    };

    println!("10.0 / 2.0 = {}", divide(10.0, 2.0));
    println!("10.0 / 0.0 = {}", divide(10.0, 0.0));
}

/// Запускает все примеры базового синтаксиса.
pub fn run_all() {
    demonstrate_basic_syntax();
    demonstrate_oneline_closures();
    demonstrate_explicit_return_type();
}
