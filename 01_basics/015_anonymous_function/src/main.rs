// Урок 015. ЗАМЫКАНИЯ — БАЗОВЫЙ СИНТАКСИС И ЗАХВАТ ПЕРЕМЕННЫХ
// ============================================================================
//
// Что такое замыкание (closure)?
//   Анонимная функция, которую можно сохранить в переменную, передать
//   как аргумент и — главное — «захватить» переменные из окружающего кода.
//
// Что разбирается в этом уроке:
//   1. Синтаксис: ||, |x|, |x: T|, однострочные и блочные
//   2. Захват по ссылке (&T), по изменяемой ссылке (&mut T), с move
//   3. Паттерны: замыкания в структурах, с Option/Result, как аргументы
//
// Что НЕ входит в этот урок (см. урок 068):
//   Трейты Fn / FnMut / FnOnce, impl Fn в сигнатурах функций,
//   Box<dyn Fn>, замыкания совместно с итераторами.
// ============================================================================

pub mod basic_syntax;
pub mod capture;
pub mod patterns;

fn main() {
    println!("╔═══════════════════════════════════════════════════════════╗");
    println!("║     Урок 015. ЗАМЫКАНИЯ — СИНТАКСИС И ЗАХВАТ ПЕРЕМЕННЫХ   ║");
    println!("╚═══════════════════════════════════════════════════════════╝");

    // Модуль 1: Базовый синтаксис
    basic_syntax::run_all();

    // Модуль 2: Захват переменных
    capture::run_all();

    // Модуль 3: Паттерны использования
    patterns::run_all();

    println!("\n╔═══════════════════════════════════════════════════════════╗");
    println!("║            ВСЕ ПРИМЕРЫ ВЫПОЛНЕНЫ УСПЕШНО! ✓               ║");
    println!("╚═══════════════════════════════════════════════════════════╝");

    println!("\n✅ Урок по базовым замыканиям (синтаксис и захват) завершён!");
}
