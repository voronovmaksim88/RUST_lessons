//! Первая программа на Rust — классический «Hello, World!».
//!
//! ## Что вы узнаете
//!
//! - Как выглядит минимальная программа на Rust.
//! - Что такое функция `main()` — точка входа в программу.
//! - Что такое макрос `println!` и зачем нужен восклицательный знак.
//! - Как скомпилировать и запустить программу через `rustc` и `cargo`.
//!
//! ## Структура программы
//!
//! | Элемент | Описание |
//! |---------|----------|
//! | `fn main()` | Точка входа — с неё начинается выполнение |
//! | `{}` | Тело функции, блок кода |
//! | `println!()` | Макрос для вывода текста на экран |
//! | `!` | Признак макроса, а не обычной функции |
//! | `";"` | Каждая инструкция в Rust заканчивается точкой с запятой |
//!
//! Подробнее о макросах — в файле `exclamation_mark.md` рядом с этим проектом.

// ============================================================
// Урок 001.
// ============================================================

fn main() {
    println!("Привет, мир!");
}

