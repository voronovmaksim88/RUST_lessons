// ============================================================================
// Урок 019. БАЗОВЫЙ ВВОД И ВЫВОД (I/O)
// ============================================================================

use std::io::{self, Write};

fn main() {
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║          Урок 019. БАЗОВЫЙ ВВОД И ВЫВОД (I/O)              ║");
    println!("╚══════════════════════════════════════════════════════════════╝");

    // ============================================
    // 1. ВЫВОД В STDOUT: print! vs println!
    // ============================================
    println!("\n1. print! vs println!");

    // println! добавляет перевод строки \n в конце — курсор переходит на новую строку.
    println!("   println! — с переводом строки");

    // print! НЕ добавляет \n — курсор остаётся на той же строке.
    // Это удобно для приглашений ввода: "Введите имя: [курсор здесь]"
    print!("   print!   — без перевода строки  |  ");
    print!("   продолжение на той же строке");
    println!(); // просто перевод строки

    // Форматирование работает одинаково в обоих макросах:
    let x = 42;
    let name = "Rust";
    println!("   Форматирование: x={x}, name={name}, pi={:.2}", 3.14159);

    // ============================================
    // 2. FLUSH — СБРОС БУФЕРА ВЫВОДА
    // ============================================
    println!("\n2. Flush — зачем сбрасывать буфер");

    // stdout в Rust буферизован построчно при выводе в терминал:
    // текст накапливается в буфере и выводится целиком при появлении \n.
    // При перенаправлении в файл (> out.txt) буферизация становится блочной —
    // flush() нужен ещё чаще, иначе текст может не появиться вовремя.
    //
    // Проблема: print!("Введите имя: ") без \n останется в буфере —
    // пользователь не увидит приглашения до своего нажатия Enter!
    //
    // Решение: явно сбросить буфер через flush() перед ожиданием ввода.
    //
    //   print!("Введите имя: ");
    //   io::stdout().flush().unwrap(); // ← буфер сброшен, текст появился на экране
    //
    // flush() возвращает Result — обрабатываем через unwrap() или expect().

    println!("   stdout буферизован построчно (в терминале):");
    println!("   print!(\"...\")           — текст в буфере, НЕ виден");
    println!("   io::stdout().flush()    — буфер сброшен, текст виден");
    println!("   println!(\"...\")         — \\n автоматически сбрасывает буфер");

    // ============================================
    // 3. ЧТЕНИЕ СТРОКИ ИЗ STDIN
    // ============================================
    println!("\n3. Чтение строки из stdin");

    // Разбираем read_line по частям:
    //
    //   let mut input = String::new();
    //   │              └── нужен изменяемый буфер — read_line дописывает в него
    //   └── mut обязателен
    //
    //   io::stdin().read_line(&mut input).expect("Ошибка чтения");
    //   │            │                    └── обрабатываем Result<usize, Error>
    //   │            └── передаём изменяемую ссылку
    //   └── получаем дескриптор stdin
    //
    // ВАЖНО: read_line читает до \n ВКЛЮЧИТЕЛЬНО — символ переноса войдёт в строку!
    // "Максим\n"  ← вот что будет в input после ввода "Максим" + Enter

    println!("   Схема работы read_line:");
    println!("   let mut buf = String::new();");
    println!("   io::stdin().read_line(&mut buf).expect(\"...\");");
    println!("   // buf содержит введённый текст + \\n в конце");

    // ============================================
    // 4. TRIM — ОЧИСТКА ВВОДА ОТ \n И ПРОБЕЛОВ
    // ============================================
    println!("\n4. Очистка ввода через .trim()");

    // Симулируем то, что приходит из stdin (включая \n):
    let raw_input = "  Максим\n";
    println!("   Сырой ввод : {:?}  (len={})", raw_input, raw_input.len());

    let trimmed = raw_input.trim();
    println!("   После trim : {:?}  (len={})", trimmed, trimmed.len());

    // trim() убирает пробельные символы с обоих концов (\n, \r, \t, пробел).
    // trim_start() — только слева, trim_end() — только справа.
    println!("   trim_end   : {:?}", raw_input.trim_end()); // убирает только \n
    println!("   trim_start : {:?}", raw_input.trim_start()); // убирает только пробелы слева

    // ============================================
    // 5. ПАРСИНГ ЧИСЛОВОГО ВВОДА
    // ============================================
    println!("\n5. Парсинг строки в число");

    // parse() возвращает Result — нужно обработать возможную ошибку.
    // Три стиля обработки на выбор:

    // Вариант 1: expect() — паника с понятным сообщением.
    // Подходит для прототипов и учебных примеров.
    let valid = "42";
    let n1: i32 = valid.parse().expect("Введите целое число!");
    println!("   expect()       : \"{}\" → {}", valid, n1);

    // Вариант 2: unwrap_or() — значение по умолчанию при ошибке.
    // Подходит когда неверный ввод — допустимый сценарий.
    let invalid = "abc";
    let n2: i32 = invalid.parse().unwrap_or(0);
    println!("   unwrap_or(0)   : \"{}\" → {} (не число → 0)", invalid, n2);

    // Вариант 3: match — полный контроль над обоими исходами.
    // Подходит для продакшн-кода.
    let n3: i32 = match "99".parse() {
        Ok(num)  => num,
        Err(e)   => { eprintln!("   Ошибка парсинга: {e}"); 0 }
    };
    println!("   match          : \"99\" → {}", n3);

    // ============================================
    // 6. ВСПОМОГАТЕЛЬНАЯ ФУНКЦИЯ ДЛЯ ВВОДА
    // ============================================
    println!("\n6. Вспомогательная функция read_line()");

    // Повторять связку String::new / read_line / trim / to_string
    // каждый раз громоздко. Выносим в функцию:
    //
    //   fn read_line() -> String {
    //       let mut buf = String::new();
    //       io::stdin().read_line(&mut buf).expect("Ошибка чтения");
    //       buf.trim().to_string()
    //   }
    //
    // Функция объявлена внизу файла, используется в разделе 8.

    println!("   Объявлена функция read_input() — используется в практике ниже.");
    println!("   fn read_input() -> String — читает строку, возвращает trim'd String.");

    // ============================================
    // 7. ВЫВОД В STDERR: eprint! / eprintln!
    // ============================================
    println!("\n7. Вывод в stderr: eprint! / eprintln!");

    // stderr — отдельный поток вывода, не зависит от stdout.
    // Используется для сообщений об ошибках и диагностики.
    eprintln!("   [stderr] Это сообщение идёт в stderr");
    eprint!("   [stderr] eprint! — без \\n,  ");
    eprintln!("продолжение через eprintln!");

    println!("   Почему это важно:");
    println!("   stdout → основной вывод программы (результаты)");
    println!("   stderr → ошибки и предупреждения (диагностика)");
    println!("   В терминале оба видны вместе, но можно разделить:");
    println!("   $ cargo run > output.txt       — stdout в файл, stderr в терминал");
    println!("   $ cargo run 2> errors.txt      — stderr в файл, stdout в терминал");
    println!("   $ cargo run > out.txt 2>&1     — оба в один файл");

    // ============================================
    // 8. ПРАКТИКА: МИНИ-КАЛЬКУЛЯТОР
    // ============================================
    println!("\n8. Практика: мини-калькулятор");
    println!("   Применяем: flush, read_line, trim, parse, match, eprintln");
    // "─".repeat(64) — метод repeat() повторяет строку N раз; здесь рисует горизонтальную линию
    println!("{}", "─".repeat(64));

    // Первое число
    print!("   Введите первое число: ");
    io::stdout().flush().unwrap();
    let a: f64 = loop {
        let s = read_input();
        match s.parse() {
            Ok(n)  => break n,
            Err(_) => {
                eprintln!("   [ошибка] Не число, попробуйте ещё раз.");
                print!("   Введите первое число: ");
                io::stdout().flush().unwrap();
            }
        }
    };

    // Операция
    print!("   Операция (+, -, *, /): ");
    io::stdout().flush().unwrap();
    let op = read_input();

    // Второе число
    print!("   Введите второе число: ");
    io::stdout().flush().unwrap();
    let b: f64 = loop {
        let s = read_input();
        match s.parse() {
            Ok(n)  => break n,
            Err(_) => {
                eprintln!("   [ошибка] Не число, попробуйте ещё раз.");
                print!("   Введите второе число: ");
                io::stdout().flush().unwrap();
            }
        }
    };

    // Вычисление
    println!("{}", "─".repeat(64));
    match op.as_str() {
        "+" => println!("   {} + {} = {}", a, b, a + b),
        "-" => println!("   {} - {} = {}", a, b, a - b),
        "*" => println!("   {} * {} = {}", a, b, a * b),
        "/" => {
            if b == 0.0 {
                eprintln!("   [ошибка] Деление на ноль!");
            } else {
                println!("   {} / {} = {:.6}", a, b, a / b);
            }
        }
        _   => eprintln!("   [ошибка] Неизвестная операция: \"{}\"", op),
    }
    println!("{}", "─".repeat(64));

    // ============================================
    // ИТОГИ УРОКА
    // ============================================
    println!("\n╔══════════════════════════════════════════════════════════════╗");
    println!("║                     ИТОГИ УРОКА                             ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  print!(...)             вывод без \\n                       ║");
    println!("║  println!(...)           вывод с \\n                         ║");
    println!("║  io::stdout().flush()    сброс буфера (нужен после print!)  ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  io::stdin().read_line(&mut s)  читает строку + \\n          ║");
    println!("║  s.trim()                       убирает \\n и пробелы        ║");
    println!("║  s.trim().parse::<T>()          строка → число (Result)      ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  eprint!/eprintln!(...)  вывод в stderr (ошибки)            ║");
    println!("║  cargo run > out.txt     перенаправить stdout в файл        ║");
    println!("╚══════════════════════════════════════════════════════════════╝");

    println!("\n✅ Урок по базовому вводу и выводу завершён!");
}

// ─────────────────────────────────────────────────────────────────────────────
// Вспомогательная функция: читает одну строку из stdin, возвращает trim'd String.
// Выносим повторяющуюся связку (new → read_line → trim → to_string) в одно место.
// ─────────────────────────────────────────────────────────────────────────────
fn read_input() -> String {
    let mut buf = String::new();
    io::stdin()
        .read_line(&mut buf)
        .expect("Ошибка чтения stdin");
    buf.trim().to_string()
}
