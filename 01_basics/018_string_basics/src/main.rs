// ============================================================================
// Урок 018. ОСНОВЫ РАБОТЫ СО СТРОКАМИ В RUST
// ============================================================================

fn main() {
    println!("╔══════════════════════════════════════════════════════════════╗");
    println!("║        Урок 018. ОСНОВЫ РАБОТЫ СО СТРОКАМИ В RUST           ║");
    println!("╚══════════════════════════════════════════════════════════════╝");

    // ============================================
    // 1. ДВА СТРОКОВЫХ ТИПА: &str И String
    // ============================================
    println!("\n1. Два строковых типа: &str и String");

    // &str — строковый срез (slice), ссылка на строку где-то в памяти.
    // Чаще всего это &'static str — литерал, зашитый прямо в бинарник.
    // Неизменяем, не владеет данными.
    let s_str: &str = "Привет"; // живёт в статической памяти

    // String — владеющая строка. Данные лежат в куче (heap).
    // Изменяема (если mut), владеет своими данными.
    let s_string: String = String::from("Привет");

    println!("   &str    : {s_str}  (ссылка, не владеет данными)");
    println!("   String  : {s_string}  (владеет данными в куче)");

    // Deref coercion: &String автоматически приводится к &str.
    // Поэтому функции, принимающие &str, работают и со String.
    let s_owned = String::from("мир");
    let s_ref: &str = &s_owned; // &String → &str автоматически
    println!("   &String → &str : {s_ref}");

    // Сравнительная таблица:
    //
    //  Свойство         │  &str             │  String
    //  ─────────────────┼───────────────────┼───────────────────
    //  Расположение     │  стек / статика   │  куча (heap)
    //  Изменяемость     │  нет              │  да (если mut)
    //  Владение         │  нет (заимствует) │  да
    //  Создание         │  "литерал"        │  String::from(...)
    //  Размер на стеке  │  2 слова (ptr+len)│  3 слова (+cap)

    // ============================================
    // 2. СОЗДАНИЕ STRING — ЧЕТЫРЕ СПОСОБА
    // ============================================
    println!("\n2. Создание String");

    // Пустая строка — когда данные появятся позже
    let s1 = String::new();
    println!("   String::new()       : \"{s1}\" (пустая)");

    // Из строкового литерала — самый распространённый способ
    let s2 = String::from("текст из литерала");
    println!("   String::from(...)   : \"{s2}\"");

    // .to_string() — метод трейта Display, работает на любом &str
    let s3 = "тот же результат".to_string();
    println!("   \"...\".to_string()   : \"{s3}\"");

    // format! — удобен когда нужно собрать строку из нескольких частей
    let name = "Rust";
    let version = 2024;
    let s4 = format!("Язык {} редакции {}", name, version);
    println!("   format!(...)        : \"{s4}\"");

    // ============================================
    // 3. ИЗМЕНЕНИЕ СТРОКИ: push, push_str, +=
    // ============================================
    println!("\n3. Изменение строки");

    let mut s = String::from("Привет");
    println!("   Исходная : \"{s}\"");

    // push — добавляет один символ (char)
    s.push(',');
    s.push(' ');
    println!("   После push(',') push(' ') : \"{s}\"");

    // push_str — добавляет &str, не перемещает аргумент
    let word = "мир";
    s.push_str(word);
    println!("   После push_str(\"мир\")      : \"{s}\"");
    println!("   word ещё жив: \"{word}\""); // word не перемещён

    // += это синтаксический сахар для push_str
    s += "!";
    println!("   После += \"!\"               : \"{s}\"");

    // ============================================
    // 4. КОНКАТЕНАЦИЯ: +, format!, push_str
    // ============================================
    println!("\n4. Конкатенация строк");

    // Способ 1: оператор +
    // Сигнатура: fn add(self, s: &str) -> String
    // Первый операнд ПЕРЕМЕЩАЕТСЯ (move), второй — заимствуется.
    let s1 = String::from("Привет, ");
    let s2 = String::from("мир!");
    let s3 = s1 + &s2; // s1 перемещён внутрь, s2 остаётся доступным
    // println!("{s1}"); // ОШИБКА: s1 уже перемещён
    println!("   s1 + &s2 = \"{s3}\"  (s1 перемещён, s2 жив: \"{s2}\")");

    // Цепочка + быстро становится громоздкой:
    let a = String::from("tic");
    let b = String::from("tac");
    let c = String::from("toe");
    let result = a + "-" + &b + "-" + &c;
    println!("   a + \"-\" + &b + \"-\" + &c = \"{result}\"");

    // Способ 2: format! — не перемещает ни один аргумент
    let x = String::from("один");
    let y = String::from("два");
    let z = String::from("три");
    let joined = format!("{x} + {y} + {z}");
    println!("   format!  = \"{joined}\"  (x, y, z живы)");
    println!("   x=\"{x}\", y=\"{y}\", z=\"{z}\"");

    // Способ 3: push_str — изменяет строку на месте, самый эффективный
    // (не создаёт промежуточных значений)
    let mut buf = String::new();
    buf.push_str("один");
    buf.push_str(", ");
    buf.push_str("два");
    buf.push_str(", ");
    buf.push_str("три");
    println!("   push_str = \"{buf}\"");

    // Когда что использовать:
    // + : склеить две строки, первая больше не нужна
    // format! : читабельно, несколько переменных, все нужны дальше
    // push_str: построение строки в цикле или поэтапно

    // ============================================
    // 5. ДЛИНА, ЁМКОСТЬ, ПУСТОТА
    // ============================================
    println!("\n5. Длина, ёмкость, пустота");

    let ascii = String::from("Hello");
    println!("   \"Hello\".len()      = {}  (байт)", ascii.len());
    println!("   \"Hello\".is_empty() = {}", ascii.is_empty());
    println!("   \"Hello\".capacity() = {}  (байт выделено в куче)", ascii.capacity());

    // ВАЖНО: len() считает БАЙТЫ, а не символы!
    let cyrillic = String::from("Привет"); // 6 символов, каждый по 2 байта = 12
    println!("\n   \"Привет\".len()           = {}  (байт)", cyrillic.len());
    println!("   \"Привет\".chars().count() = {}  (символов)", cyrillic.chars().count());

    let emoji = String::from("🦀"); // краб занимает 4 байта в UTF-8
    println!("\n   \"🦀\".len()           = {}  (байт)", emoji.len());
    println!("   \"🦀\".chars().count() = {}  (символ)", emoji.chars().count());

    let empty = String::new();
    println!("\n   Пустая строка: is_empty = {}", empty.is_empty());

    // ============================================
    // 6. ПОЧЕМУ В RUST НЕЛЬЗЯ s[0]
    // ============================================
    println!("\n6. Почему нельзя s[0] — UTF-8 и байты");

    // Rust запрещает индексацию строк оператором [].
    // Причина: строки в Rust — это UTF-8, где символы имеют разный размер.
    //
    // let s = String::from("Привет");
    // let c = s[0]; // ОШИБКА компиляции: cannot index into a value of type `String`
    //
    // Если бы это было разрешено, s[0] вернул бы байт 0xD0 —
    // это первый байт символа 'П' в UTF-8, но не сам символ.
    // Rust защищает от этой ловушки на уровне типов.

    println!("   Байты строки \"Привет\" (каждый символ = 2 байта):");
    for (i, b) in "Привет".bytes().enumerate() {
        print!("   [{i}]={:#04X}", b);
    }
    println!();

    println!("\n   Три уровня представления строки:");
    println!("   bytes()  → отдельные байты (u8), для бинарной обработки");
    println!("   chars()  → скалярные значения Unicode (char), для текста");
    println!("   графемные кластеры → крейт unicode-segmentation (сложные скрипты)");

    // ============================================
    // 7. СРЕЗЫ СТРОК — ОСТОРОЖНО С БАЙТОВЫМИ ИНДЕКСАМИ
    // ============================================
    println!("\n7. Срезы строк &s[начало..конец]");

    // Индексы в срезах — БАЙТОВЫЕ, а не символьные.
    // Если граница попадёт внутрь многобайтового символа — паника в runtime!
    let s = String::from("Hello, world!");
    let hello = &s[0..5];  // байты 0–4 → "Hello"
    let world = &s[7..12]; // байты 7–11 → "world"
    println!("   \"Hello, world!\"[0..5]  = \"{hello}\"");
    println!("   \"Hello, world!\"[7..12] = \"{world}\"");

    // Кириллица: каждый символ = 2 байта.
    // П=0..2, р=2..4, и=4..6, в=6..8, е=8..10, т=10..12
    let cyr = "Привет";
    let pi   = &cyr[0..2];  // "П"
    let ri   = &cyr[2..4];  // "р"
    let full = &cyr[0..12]; // "Привет"
    println!("\n   \"Привет\"[0..2]  = \"{pi}\"  (1 символ = 2 байта)");
    println!("   \"Привет\"[2..4]  = \"{ri}\"");
    println!("   \"Привет\"[0..12] = \"{full}\"");

    // &cyr[0..1] вызвало бы панику — разрезает символ 'П' пополам!
    // thread 'main' panicked at 'byte index 1 is not a char boundary'

    // Безопасная альтернатива: chars().nth(n) — доступ по номеру символа
    let third_char = "Привет".chars().nth(2); // 2-й индекс = 'и'
    println!("\n   Безопасный доступ: \"Привет\".chars().nth(2) = {:?}", third_char);

    // ============================================
    // 8. ИТЕРАЦИЯ ПО СТРОКЕ
    // ============================================
    println!("\n8. Итерация по строке");

    // По символам — правильный способ для работы с текстом
    let word = "Rust🦀";
    print!("   chars()  \"{word}\": ");
    for c in word.chars() {
        print!("'{c}' ");
    }
    println!();

    // По байтам — для бинарной или сетевой обработки
    print!("   bytes()  \"Hi!\": ");
    for b in "Hi!".bytes() {
        print!("{b} ");
    }
    println!();

    // По строкам — удобно для многострочного текста
    println!("   lines()  многострочный текст:");
    let text = "первая строка\nвторая строка\nтретья строка";
    for (i, line) in text.lines().enumerate() {
        println!("     [{i}] {line}");
    }

    // enumerate() — символ вместе с его порядковым номером
    println!("   chars().enumerate() для \"Rust\":");
    for (i, c) in "Rust".chars().enumerate() {
        println!("     позиция {i}: '{c}'");
    }

    // ============================================
    // 9. КОНВЕРТАЦИЯ String ↔ &str
    // ============================================
    println!("\n9. Конвертация String ↔ &str");

    // String → &str (три способа)
    let owned = String::from("текст");
    let borrowed1: &str = &owned;         // deref coercion — неявно
    let borrowed2: &str = owned.as_str(); // явно через метод
    let borrowed3: &str = &owned[..];     // через срез всей строки
    println!("   String → &str:");
    println!("     &owned      = \"{borrowed1}\"");
    println!("     .as_str()   = \"{borrowed2}\"");
    println!("     &owned[..] = \"{borrowed3}\"");

    // &str → String (три способа)
    let literal = "литерал";
    let s1 = literal.to_string(); // через Display
    let s2 = String::from(literal); // конструктор
    let s3 = literal.to_owned(); // клонирует данные
    println!("\n   &str → String:");
    println!("     .to_string()  = \"{s1}\"");
    println!("     String::from  = \"{s2}\"");
    println!("     .to_owned()   = \"{s3}\"");

    // Deref coercion на практике: функция принимает &str,
    // но можно передать и &String — компилятор разыменует автоматически.
    fn greet(name: &str) {
        println!("     Привет, {name}!");
    }
    println!("\n   Deref coercion в действии:");
    greet("Мир");                    // &str напрямую
    greet(&String::from("Rust"));    // &String → &str автоматически

    // ============================================
    // 10. КРАТКИЙ ОБЗОР МЕТОДОВ (ДЕТАЛЬНО — В УРОКЕ 045)
    // ============================================
    println!("\n10. Краткий обзор полезных методов");

    let s = "  Привет, Мир!  ";
    println!("   Исходная строка: \"{s}\"");

    // Пробелы
    println!("   .trim()        = \"{}\"", s.trim());
    println!("   .trim_start()  = \"{}\"", s.trim_start());
    println!("   .trim_end()    = \"{}\"", s.trim_end());

    // Регистр
    let sample = "Привет, Мир!";
    println!("\n   Исходная: \"{sample}\"");
    println!("   .to_uppercase() = \"{}\"", sample.to_uppercase());
    println!("   .to_lowercase() = \"{}\"", sample.to_lowercase());

    // Поиск
    println!("\n   .contains(\"Мир\")    = {}", sample.contains("Мир"));
    println!("   .starts_with(\"При\") = {}", sample.starts_with("При"));
    println!("   .ends_with('!')     = {}", sample.ends_with('!'));

    // Замена
    println!("   .replace(\"Мир\",\"Rust\") = \"{}\"", sample.replace("Мир", "Rust"));

    // Разбиение
    println!("\n   \"один, два, три\".split(\", \"):");
    for part in "один, два, три".split(", ") {
        println!("     → \"{part}\"");
    }

    println!("\n   💡 Все методы подробно — в уроке 045 (Методы String и UTF-8)");

    // ============================================
    // ИТОГИ УРОКА
    // ============================================
    println!("\n╔══════════════════════════════════════════════════════════════╗");
    println!("║                    ИТОГИ УРОКА                              ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  &str   — ссылка на строку, неизменяема, не владеет данными ║");
    println!("║  String — владеющая строка в куче, изменяема (если mut)     ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  Создание: String::new() / String::from() / format!()       ║");
    println!("║  Изменение: push(char), push_str(&str), += &str             ║");
    println!("║  Склейка: + перемещает первый операнд! format! — безопасен  ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  .len() считает БАЙТЫ, а не символы                         ║");
    println!("║  s[0] запрещён — используй .chars().nth(n)                  ║");
    println!("║  Срезы &s[0..n] по байтам — осторожно с кириллицей!         ║");
    println!("╠══════════════════════════════════════════════════════════════╣");
    println!("║  Итерация: .chars() — символы, .bytes() — байты             ║");
    println!("║  &String → &str автоматически (deref coercion)              ║");
    println!("╚══════════════════════════════════════════════════════════════╝");

    println!("\n✅ Урок по основам работы со строками завершён!");
}
