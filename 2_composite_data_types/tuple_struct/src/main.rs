// ============================================================
// Урок: Tuple Struct (кортежная структура) в Rust
// ============================================================
//
// Tuple Struct — это нечто среднее между обычным кортежем (tuple) и
// именованной структурой (struct). У неё есть имя типа, но поля
// безымянны и доступны только по индексу (0, 1, 2, ...).
//
// Зачем нужны tuple struct?
// -------------------------
// 1. **Именование кортежа** — дать осмысленное имя группе значений,
//    не придумывая имена для каждого поля.
// 2. **Newtype-паттерн** — обернуть один тип в новый, чтобы создать
//    "новый тип" с теми же данными, но другой семантикой (например,
//    Meter(f64) и Kilometer(f64) — оба содержат f64, но это разные типы).
// 3. **Прозрачность** — когда имена полей избыточны (например, Point(i32, i32)
//    и так понятно, что это x и y).
//
// Синтаксис:
// ----------
//   struct ИмяТипа(Тип1, Тип2, ...);
//
// Доступ к полям:
// ---------------
//   экземпляр.0, экземпляр.1, ...

// ------------------------------------------------------------------
// Пример 1: Простейший tuple struct — Point
// ------------------------------------------------------------------
struct Point(i32, i32);

// ------------------------------------------------------------------
// Пример 2: Newtype-паттерн — обёртка над примитивным типом
// ------------------------------------------------------------------
// Допустим, мы хотим различать метры и километры на уровне типов:
struct Meter(f64);
struct Kilometer(f64);

// Хотя оба содержат f64, Meter и Kilometer — РАЗНЫЕ типы.
// Нельзя случайно передать Meter туда, где ожидается Kilometer.

// ------------------------------------------------------------------
// Пример 3: Tuple struct с тремя полями
// ------------------------------------------------------------------
struct Color(u8, u8, u8); // RGB

// ------------------------------------------------------------------
// Пример 4: Tuple struct, содержащий другой tuple struct
// ------------------------------------------------------------------
struct Distance(Point, Point);

// ============================================================
// Методы (impl-блоки) для tuple struct
// ============================================================
impl Point {
    // Ассоциированная функция (конструктор)
    fn new(x: i32, y: i32) -> Self {
        Self(x, y)
    }

    // Метод: вычисляет расстояние до другой точки
    fn distance_to(&self, other: &Point) -> f64 {
        let dx = (self.0 - other.0) as f64;
        let dy = (self.1 - other.1) as f64;
        (dx * dx + dy * dy).sqrt()
    }

    // Метод: перемещает точку на (dx, dy) (требует &mut self)
    fn translate(&mut self, dx: i32, dy: i32) {
        self.0 += dx;
        self.1 += dy;
    }

    // Метод: потребляет точку и возвращает сумму координат
    fn sum(self) -> i32 {
        self.0 + self.1
    }
}

impl Meter {
    fn to_kilometers(&self) -> f64 {
        self.0 / 1000.0
    }

    fn from_kilometers(km: f64) -> Self {
        Self(km * 1000.0)
    }
}

impl Color {
    // Яркость по формуле восприятия (0.0 - 1.0)
    fn brightness(&self) -> f64 {
        (0.299 * self.0 as f64 + 0.587 * self.1 as f64 + 0.114 * self.2 as f64) / 255.0
    }

    fn as_hex_string(&self) -> String {
        format!("#{:02X}{:02X}{:02X}", self.0, self.1, self.2)
    }
}

// ============================================================
// Unit-like struct (структура без полей)
// ============================================================
// Это вырожденный случай tuple struct с нулём полей.
// Используется как маркер или для реализации типажей без данных.
struct AlwaysTrue;

impl AlwaysTrue {
    fn check(&self) -> bool {
        true
    }
}

// ============================================================
// main — демонстрация всех концепций
// ============================================================
fn main() {
    println!("========== Tuple Struct в Rust ==========\n");

    // --- Создание экземпляров ---
    let start = Point(2, 4);
    let end = Point(8, 12);
    println!("Точка start:  ({}, {})", start.0, start.1);
    println!("Точка end:    ({}, {})", end.0, end.1);

    // --- Доступ к полям по индексу ---
    let dist = Distance(start, end);
    println!(
        "Distance: начало=({},{})  конец=({},{})",
        dist.0.0, // dist.0 — это Point, dist.0.0 — его x
        dist.0.1,
        dist.1.0,
        dist.1.1
    );

    // --- Вызов методов ---
    let a = Point::new(0, 0);
    let b = Point::new(3, 4);
    println!(
        "\nРасстояние между ({},{}) и ({},{}): {:.2}",
        a.0,
        a.1,
        b.0,
        b.1,
        a.distance_to(&b)
    );

    let mut p = Point(5, 5);
    p.translate(10, -3);
    println!("После сдвига (5,5) на (10,-3): ({}, {})", p.0, p.1);

    let p2 = Point(7, 3);
    let s = p2.sum();
    // p2 больше недоступен — ownership перешёл в метод sum()
    println!("Сумма координат (7,3): {}", s);

    // --- Newtype-паттерн ---
    let distance_m = Meter(1500.0);
    println!(
        "\n{} метров = {} километров",
        distance_m.0,
        distance_m.to_kilometers()
    );
    let km = Kilometer(2.5);
    let m_from_km = Meter::from_kilometers(km.0);
    println!("{} км = {} метров", km.0, m_from_km.0);

    // --- Color ---
    let coral = Color(255, 127, 80);
    println!(
        "\nЦвет: {} (яркость: {:.2})",
        coral.as_hex_string(),
        coral.brightness()
    );

    // --- Unit-like struct ---
    let flag = AlwaysTrue;
    println!("\nAlwaysTrue.check() = {}", flag.check());

    // --- Деструктуризация tuple struct (аналогично кортежам) ---
    println!("\n--- Деструктуризация ---");
    let Color(r, g, b) = coral;
    println!("Красный: {}, Зелёный: {}, Синий: {}", r, g, b);

    let Point(x, y) = Point(42, 99);
    println!("x = {}, y = {}", x, y);

    // --- Сравнение: tuple struct хранится так же, как кортеж ---
    let tuple_point: (i32, i32) = (10, 20);
    let struct_point = Point(10, 20);
    // Память занимают одинаково, но это разные типы:
    // нельзя передать struct_point туда, где ожидается (i32, i32), и наоборот.
    println!("\nКортеж:      ({}, {})", tuple_point.0, tuple_point.1);
    println!("Tuple struct: ({}, {})", struct_point.0, struct_point.1);
}
