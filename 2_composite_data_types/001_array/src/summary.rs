pub fn run() {
    // ========================================================================
    // РЕЗЮМЕ
    // ========================================================================
    println!("┌──────────────────────────────────────────────────────────────────────┐");
    println!("│                                РЕЗЮМЕ                                │");
    println!("├──────────────────────────────────────────────────────────────────────┤");
    println!("│ БАЗОВЫЕ КОНЦЕПЦИИ:                                                   │");
    println!("│ • [T; N]        - тип массива, размер N входит в тип                 │");
    println!("│ • фиксированный - размер известен на этапе компиляции                │");
    println!("│ • индексация    - доступ по индексу с 0                              │");
    println!("│ • mut           - для изменения элементов                            │");
    println!("│                                                                      │");
    println!("│ СРЕЗЫ:                                                               │");
    println!("│ • &[T]          - срез массива (view без владения)                   │");
    println!("│ • &arr[2..5]    - срез от индекса 2 до 5 (не включая)                │");
    println!("│ • &mut arr[..]  - изменяемый срез                                    │");
    println!("│                                                                      │");
    println!("│ ИТЕРИРОВАНИЕ:                                                        │");
    println!("│ • .iter()       - итератор по ссылкам                                │");
    println!("│ • .iter_mut()   - изменяемый итератор                                │");
    println!("│ • .enumerate()  - с индексами                                        │");
    println!("│ • .rev()        - обратный порядок                                   │");
    println!("│                                                                      │");
    println!("│ БЕЗОПАСНОСТЬ:                                                        │");
    println!("│ • .get(i)       - безопасный доступ (возвращает Option)              │");
    println!("│ • arr[i]        - паника при выходе за границы                       │");
    println!("│                                                                      │");
    println!("│ ПОЛЕЗНЫЕ МЕТОДЫ:                                                     │");
    println!("│ • .len()        - длина массива                                      │");
    println!("│ • .first/last() - первый/последний элемент                           │");
    println!("│ • .contains()   - проверка наличия элемента                          │");
    println!("│ • .sort()       - сортировка                                         │");
    println!("│ • .reverse()    - разворот массива                                   │");
    println!("│ • .fill()       - заполнение значением                               │");
    println!("│ • .swap()       - обмен элементов                                    │");
    println!("│                                                                      │");
    println!("│ ИНИЦИАЛИЗАЦИЯ:                                                       │");
    println!("│ • [val; N]      - N копий значения val                               │");
    println!("│ • from_fn()     - создание через функцию                             │");
    println!("│                                                                      │");
    println!("│ ФУНКЦИИ:                                                             │");
    println!("│ • &[T]          - приём срезов (работает с любым размером)           │");
    println!("│ • [T; N]        - конкретный размер                                  │");
    println!("│ • const N       - обобщение по размеру                               │");
    println!("│                                                                      │");
    println!("│ ARRAY vs VEC:                                                        │");
    println!("│ • Array         - фиксированный размер, на стеке, быстрее            │");
    println!("│ • Vec           - динамический размер, на куче, гибче                │");
    println!("└──────────────────────────────────────────────────────────────────────┘");
}
