pub fn run() {
    // ========================================================================
    // 8. МНОГОМЕРНЫЕ МАССИВЫ
    // ========================================================================
    println!("┌── 8.1 Двумерный массив и доступ ─────────────────────────────────────┐");
    let matrix: [[i32; 3]; 2] = [[1, 2, 3], [4, 5, 6]];
    let row0: [i32; 3] = matrix[0];
    let row1: [i32; 3] = matrix[1];
    println!("  row0 = {:?}", row0);
    println!("  row1 = {:?}", row1);
    println!("  matrix[0][1] = {}", matrix[0][1]);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 8.2 Перебор двумерного массива ─────────────────────────────────────┐");
    for row in matrix {
        println!("  {:?}", row);
    }
    println!();

    for row in matrix {
        for number in row {
            print!("{} ", number);
        }
        println!();
    }
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    // ========================================================================
    // 9. СРЕЗЫ (SLICES)
    // ========================================================================
    println!("┌── 9.1 Создание среза из массива ─────────────────────────────────────┐");
    let arr = [1, 2, 3, 4, 5, 6, 7, 8];
    let slice = &arr[2..5]; // элементы с индекса 2 до 5 (не включая 5)
    println!("  массив: {:?}", arr);
    println!("  срез [2..5]: {:?}", slice);

    let full_slice = &arr[..]; // весь массив как срез
    println!("  полный срез: {:?}", full_slice);

    let from_start = &arr[..4]; // с начала до индекса 4
    println!("  срез [..4]: {:?}", from_start);

    let to_end = &arr[3..]; // с индекса 3 до конца
    println!("  срез [3..]: {:?}", to_end);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 9.2 Изменяемые срезы ──────────────────────────────────────────────┐");
    let mut arr_mut = [10, 20, 30, 40, 50];
    let slice_mut = &mut arr_mut[1..4];
    slice_mut[0] = 200; // изменяем второй элемент массива
    println!("  изменённый массив: {:?}", arr_mut);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
