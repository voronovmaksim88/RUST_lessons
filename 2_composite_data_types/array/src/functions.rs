pub fn run() {
    // ========================================================================
    // 13. МАССИВЫ В ФУНКЦИЯХ
    // ========================================================================
    println!("┌── 13.1 Передача массива по ссылке ───────────────────────────────────┐");

    // Функция принимает срез (&[i32]) — это ссылка на массив без копирования.
    fn print_array(arr: &[i32]) {
        print!("  массив: ");
        // Итерация по ссылке на массив: item имеет тип &i32.
        for item in arr {
            print!("{} ", item);
        }
        println!();
    }

    // Возвращает сумму элементов, проходя по срезу.
    fn sum_array(arr: &[i32]) -> i32 {
        let mut sum = 0;
        // Берём значения по ссылке, разыменовывая &i32.
        for &item in arr {
            sum += item;
        }
        sum
    }

    // Исходный массив остаётся на месте, мы передаём только ссылки.
    let func_arr = [1, 2, 3, 4, 5];
    print_array(&func_arr);
    println!("  сумма: {}", sum_array(&func_arr));
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 13.2 Возврат массива из функции ───────────────────────────────────┐");

    fn create_sequence() -> [i32; 5] {
        [2, 4, 6, 8, 10]
    }

    fn multiply_array(arr: [i32; 3], multiplier: i32) -> [i32; 3] {
        [
            arr[0] * multiplier,
            arr[1] * multiplier,
            arr[2] * multiplier,
        ]
    }

    let seq_result = create_sequence();
    println!("  созданная последовательность: {:?}", seq_result);

    let mult_result = multiply_array([1, 2, 3], 10);
    println!("  умноженный массив: {:?}", mult_result);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 13.3 Обобщённая функция для разных размеров ───────────────────────┐");

    fn find_max<const N: usize>(arr: &[i32; N]) -> Option<i32> {
        if N == 0 {
            return None;
        }
        let mut max = arr[0];
        for &item in arr {
            if item > max {
                max = item;
            }
        }
        Some(max)
    }

    let arr3 = [5, 2, 8];
    let arr5 = [15, 3, 42, 7, 23];

    println!("  max в {:?} = {:?}", arr3, find_max(&arr3));
    println!("  max в {:?} = {:?}", arr5, find_max(&arr5));
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
