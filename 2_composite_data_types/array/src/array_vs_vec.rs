pub fn run() {
    // ========================================================================
    // 14. ARRAY VS VEC
    // ========================================================================
    println!("┌── 14. Array vs Vec: когда что использовать ──────────────────────────┐");
    println!("  ╔══════════════════╦═══════════════════════════════════════════════╗");
    println!("  ║  Array [T; N]    ║  Vec<T>                                       ║");
    println!("  ╠══════════════════╬═══════════════════════════════════════════════╣");
    println!("  ║ • фиксированный  ║ • динамический размер                         ║");
    println!("  ║   размер         ║                                               ║");
    println!("  ║ • размер в типе  ║ • размер определяется в runtime               ║");
    println!("  ║ • на стеке       ║ • данные на куче                              ║");
    println!("  ║ • быстрее        ║ • гибче (можно добавлять/удалять элементы)    ║");
    println!("  ║ • меньше overhead║ • больше функциональности                     ║");
    println!("  ╚══════════════════╩═══════════════════════════════════════════════╝");
    println!();
    println!("  💡 Используйте Array, когда:");
    println!("     - размер известен на этапе компиляции");
    println!("     - не нужно изменять размер");
    println!("     - важна производительность");
    println!();
    println!("  💡 Используйте Vec, когда:");
    println!("     - размер неизвестен или изменяется");
    println!("     - нужно добавлять/удалять элементы");
    println!("     - работаете с данными переменного размера");

    // Пример преобразования
    let array_to_vec = [1, 2, 3, 4, 5];
    let vec_version = array_to_vec.to_vec();
    println!();
    println!("  преобразование array → vec: {:?}", vec_version);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
