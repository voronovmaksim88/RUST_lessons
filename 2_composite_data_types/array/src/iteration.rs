pub fn run() {
    // ========================================================================
    // 4. ПЕРЕБОР МАССИВА
    // ========================================================================
    println!("┌── 4.1 Перебор значений ───────────────────────────────────────────────┐");
    let users = ["Tom", "Bob", "Sam"];
    for user in users {
        print!("{} ", user);
    }
    println!();

    let fib = [1, 2, 3, 5, 8, 13, 21];
    for n in fib {
        print!("{} ", n);
    }
    println!();
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 4.2 Перебор с индексами ───────────────────────────────────────────┐");
    for i in 0..users.len() {
        println!("  users[{}] = {}", i, users[i]);
    }
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
