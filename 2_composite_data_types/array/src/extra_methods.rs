pub fn run() {
    // ========================================================================
    // 12. ДОПОЛНИТЕЛЬНЫЕ МЕТОДЫ МАССИВОВ
    // ========================================================================
    println!("┌── 12.1 contains() - проверка наличия элемента ───────────────────────┐");
    let fruits = ["яблоко", "банан", "апельсин"];
    println!("  есть 'банан': {}", fruits.contains(&"банан"));
    println!("  есть 'груша': {}", fruits.contains(&"груша"));
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 12.2 first() и last() ─────────────────────────────────────────────┐");
    let series = [100, 200, 300, 400, 500];
    println!("  первый: {:?}", series.first());
    println!("  последний: {:?}", series.last());

    let empty: [i32; 0] = [];
    println!("  первый в пустом: {:?}", empty.first());
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 12.3 reverse() - разворот массива ─────────────────────────────────┐");
    let mut order = [1, 2, 3, 4, 5];
    println!("  до reverse: {:?}", order);
    order.reverse();
    println!("  после reverse: {:?}", order);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 12.4 fill() - заполнение массива ──────────────────────────────────┐");
    let mut filled = [0; 5];
    println!("  до fill: {:?}", filled);
    filled.fill(42);
    println!("  после fill(42): {:?}", filled);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 12.5 swap() - обмен элементов ─────────────────────────────────────┐");
    let mut swappable = [10, 20, 30, 40];
    println!("  до swap: {:?}", swappable);
    swappable.swap(0, 3); // меняем местами первый и последний
    println!("  после swap(0, 3): {:?}", swappable);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
