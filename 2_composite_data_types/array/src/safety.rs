pub fn run() {
    // ========================================================================
    // 11. БЕЗОПАСНЫЙ ДОСТУП К ЭЛЕМЕНТАМ
    // ========================================================================
    println!("┌── 11.1 Метод get() для безопасного доступа ──────────────────────────┐");
    let safe_arr = [10, 20, 30];

    match safe_arr.get(1) {
        Some(value) => println!("  safe_arr[1] = {}", value),
        None => println!("  Индекс вне границ"),
    }

    match safe_arr.get(10) {
        Some(value) => println!("  safe_arr[10] = {}", value),
        None => println!("  safe_arr[10]: Индекс вне границ ❌"),
    }

    // Использование с if let
    if let Some(first) = safe_arr.get(0) {
        println!("  первый элемент: {}", first);
    }
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 11.2 Паника при выходе за границы ─────────────────────────────────┐");
    println!("  ⚠️  Раскомментируйте для демонстрации паники:");
    println!("  // let panic_arr = [1, 2, 3];");
    println!("  // let x = panic_arr[10]; // PANIC: index out of bounds");
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
