pub fn run() {
    // ========================================================================
    // 10. МЕТОДЫ ИТЕРИРОВАНИЯ
    // ========================================================================
    println!("┌── 10.1 iter() и enumerate() ─────────────────────────────────────────┐");
    let colors = ["красный", "зелёный", "синий"];

    println!("  С enumerate():");
    for (index, color) in colors.iter().enumerate() {
        println!("    [{}] = {}", index, color);
    }
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 10.2 Обратный перебор с rev() ─────────────────────────────────────┐");
    let countdown = [5, 4, 3, 2, 1];
    print!("  обратный порядок: ");
    for n in countdown.iter().rev() {
        print!("{} ", n);
    }
    println!();
    println!("└──────────────────────────────────────────────────────────────────────┘\n");

    println!("┌── 10.3 iter_mut() для изменения элементов ───────────────────────────┐");
    let mut values = [1, 2, 3, 4, 5];
    for val in values.iter_mut() {
        *val *= 2; // удваиваем каждый элемент
    }
    println!("  удвоенные значения: {:?}", values);
    println!("└──────────────────────────────────────────────────────────────────────┘\n");
}
