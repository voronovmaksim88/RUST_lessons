// ========================================================================
// АРХИТЕКТУРНО-ЗАВИСИМЫЕ ТИПЫ (isize и usize)
// ========================================================================
// Размер зависит от архитектуры процессора:
// - 64 бита на 64-битной системе
// - 32 бита на 32-битной системе
// Используются для индексации коллекций и работы с памятью.

pub fn show() {
    println!("\n╔════════════════════════════════════════════════════════════════╗");
    println!("║      АРХИТЕКТУРНО-ЗАВИСИМЫЕ ТИПЫ (isize и usize)          ║");
    println!("╚════════════════════════════════════════════════════════════════╝\n");

    let var_isize: isize = -100; // со знаком
    let var_usize: usize = 100; // без знака (используется для индексов массивов)

    println!("isize: {}", var_isize);
    println!("  Размер: {} байт", std::mem::size_of::<isize>());
    println!("  Может хранить отрицательные значения");

    println!("\nusize: {}", var_usize);
    println!("  Размер: {} байт", std::mem::size_of::<usize>());
    println!("  ⭐ Используется для индексов массивов и коллекций");

    // Определение текущей архитектуры
    let arch = if std::mem::size_of::<usize>() == 8 {
        "64-битная"
    } else {
        "32-битная"
    };
    println!("\n💻 Ваша система: {}", arch);

    // Пример использования usize для индексации
    println!("\n📋 Пример использования usize:");
    let arr = [10, 20, 30, 40, 50];
    let index: usize = 2;
    println!("  Массив: {:?}", arr);
    println!("  Элемент с индексом {}: {}", index, arr[index]);

    println!("\n📝 Когда использовать:");
    println!("  • usize - для индексов массивов, размеров коллекций");
    println!("  • isize - для арифметики с указателями, смещений в памяти");
    println!("  • Размер этих типов = размеру указателя на вашей системе");
}
