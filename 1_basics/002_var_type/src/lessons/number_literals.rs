// ========================================================================
// СПОСОБЫ ЗАПИСИ ЦЕЛЫХ ЧИСЕЛ (литералы)
// ========================================================================

pub fn show() {
    println!("\n╔════════════════════════════════════════════════════════════════╗");
    println!("║         СПОСОБЫ ЗАПИСИ ЦЕЛЫХ ЧИСЕЛ (литералы)             ║");
    println!("╚════════════════════════════════════════════════════════════════╝\n");

    let decimal = 98_222; // Десятичная система (можно с разделителями _)
    let hex = 0xff; // Шестнадцатеричная (0x)
    let octal = 0o77; // Восьмеричная (0o)
    let binary = 0b1111_0000; // Двоичная (0b)

    // Байтовый литерал `b'B'` — это специальный синтаксис
    // в Rust для представления одного байта (символа ASCII) как числа типа `u8`.
    let byte = b'B'; // Байт (только для u8). Это эквивалентно: let byte: u8 = 66;

    println!("Различные системы счисления:");
    println!("  Десятичное:        98_222 = {}", decimal);
    println!("  Шестнадцатеричное: 0xff   = {} (hex)", hex);
    println!("  Восьмеричное:      0o77   = {} (octal)", octal);
    println!("  Двоичное:          0b1111_0000 = {} (binary)", binary);
    println!(
        "  Байтовый литерал:  b'B'   = {} (ASCII код символа 'B')",
        byte
    );

    println!("\n📝 Префиксы для разных систем счисления:");
    println!("  • 0x - шестнадцатеричная (hexadecimal)");
    println!("  • 0o - восьмеричная (octal)");
    println!("  • 0b - двоичная (binary)");
    println!("  • b'X' - байтовый литерал (только ASCII символы)");

    println!("\n💡 Дополнительные примеры:");

    // Разделители для читаемости
    let million = 1_000_000;
    let binary_byte = 0b1111_1111;
    let hex_color = 0xFF_AA_BB;

    println!("  Миллион с разделителями: 1_000_000 = {}", million);
    println!("  Двоичный байт: 0b1111_1111 = {}", binary_byte);
    println!("  Hex цвет RGB: 0xFF_AA_BB = {}", hex_color);

    // Байтовые литералы
    println!("\n🔤 Байтовые литералы (тип u8):");
    let byte_a = b'A';
    let byte_space = b' ';
    let byte_newline = b'\n';
    println!("  b'A' = {} (ASCII код 'A')", byte_a);
    println!("  b' ' = {} (ASCII код пробела)", byte_space);
    println!("  b'\\n' = {} (ASCII код новой строки)", byte_newline);

    // Байтовые строки
    let byte_string = b"Hello";
    println!("\n  Байтовая строка b\"Hello\": {:?}", byte_string);
    println!("  Тип: &[u8; 5] (массив байтов)");

    println!("\n⚠️ Важно:");
    println!("  • Байтовые литералы работают только с ASCII (0-127)");
    println!("  • Нельзя: b'Я' или b'😊' (не ASCII)");
    println!("  • Можно: b'A', b'z', b'0', b'\\n' и т.д.");
}
