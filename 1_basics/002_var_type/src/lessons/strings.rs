    let raw_multiline = r#"
    {
        "name": "John",
        "age": 30
    }
    "#;
    println!("\n  –°—ã—Ä–∞—è —Å—Ç—Ä–æ–∫–∞ —Å —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—è–º–∏ r#\"...\"#:");
    println!("{}", raw_multiline);

    // –≠–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤
    println!("\n‚ïê‚ïê‚ïê –≠–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ (escape-–ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏) ‚ïê‚ïê‚ïê");
