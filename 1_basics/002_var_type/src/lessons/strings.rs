    println!("    ‚Ä¢ –ù–µ–∏–∑–º–µ–Ω—è–µ–º–∞—è —Å—Å—ã–ª–∫–∞ –Ω–∞ —Å—Ç—Ä–æ–∫–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ");
    println!("    ‚Ä¢ –•—Ä–∞–Ω–∏—Ç—Å—è –Ω–∞ —Å—Ç–µ–∫–µ (–µ—Å–ª–∏ –ª–∏—Ç–µ—Ä–∞–ª) –∏–ª–∏ –∫–∞–∫ —Å—Ä–µ–∑");
    println!("    ‚Ä¢ –†–∞–∑–º–µ—Ä –∏–∑–≤–µ—Å—Ç–µ–Ω –Ω–∞ —ç—Ç–∞–ø–µ –∫–æ–º–ø–∏–ª—è—Ü–∏–∏ (–¥–ª—è –ª–∏—Ç–µ—Ä–∞–ª–æ–≤)");
    println!("    ‚Ä¢ –ó–∞–ø–∏—Å—ã–≤–∞–µ—Ç—Å—è –∫–∞–∫ \"—Ç–µ–∫—Å—Ç\"");
    println!("    ‚Ä¢ –ù–µ –≤–ª–∞–¥–µ–µ—Ç –¥–∞–Ω–Ω—ã–º–∏");
    println!("\n  String:");
    println!("    ‚Ä¢ –ò–∑–º–µ–Ω—è–µ–º–∞—è, —Ä–∞—Å—Ç—É—â–∞—è —Å—Ç—Ä–æ–∫–∞");
    println!("    ‚Ä¢ –•—Ä–∞–Ω–∏—Ç—Å—è –≤ –∫—É—á–µ (heap)");
    println!("    ‚Ä¢ –ú–æ–∂–µ—Ç –∏–∑–º–µ–Ω—è—Ç—å —Ä–∞–∑–º–µ—Ä –≤–æ –≤—Ä–µ–º—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è");
    println!("    ‚Ä¢ –°–æ–∑–¥–∞—ë—Ç—Å—è –∫–∞–∫ String::from(\"—Ç–µ–∫—Å—Ç\")");
    println!("    ‚Ä¢ –í–ª–∞–¥–µ–µ—Ç —Å–≤–æ–∏–º–∏ –¥–∞–Ω–Ω—ã–º–∏");

    // –°—Ç—Ä–æ–∫–æ–≤—ã–µ –ª–∏—Ç–µ—Ä–∞–ª—ã - —Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏–µ
    println!("\n‚ïê‚ïê‚ïê –°—Ç—Ä–æ–∫–æ–≤—ã–µ –ª–∏—Ç–µ—Ä–∞–ª—ã (—Å—Ç–∞—Ç–∏—á–µ—Å–∫–∏–µ) ‚ïê‚ïê‚ïê");
