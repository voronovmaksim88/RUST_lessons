    // –ë–∞–∑–æ–≤—ã–µ —Å—Ç—Ä–æ–∫–∏
    println!("\n‚ïê‚ïê‚ïê –ë–∞–∑–æ–≤—ã–µ —Å—Ç—Ä–æ–∫–æ–≤—ã–µ —Å—Ä–µ–∑—ã ‚ïê‚ïê‚ïê");
