    let with_tab = "–ö–æ–ª–æ–Ω–∫–∞1\t–ö–æ–ª–æ–Ω–∫–∞2";
    let with_backslash = "–ü—É—Ç—å: C:\\Users\\Name";
    println!("  –° –∫–∞–≤—ã—á–∫–∞–º–∏: {}", with_quotes);
    println!("  –° –ø–µ—Ä–µ–Ω–æ—Å–æ–º:\n{}", with_newline);
    println!("  –° —Ç–∞–±—É–ª—è—Ü–∏–µ–π: {}", with_tab);
    println!("  –° –æ–±—Ä–∞—Ç–Ω—ã–º —Å–ª—ç—à–µ–º: {}", with_backslash);

    // –í—Å–µ escape-–ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏
    println!("\n‚ïê‚ïê‚ïê –í—Å–µ escape-–ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ ‚ïê‚ïê‚ïê");
    println!(r#"  \n  - –Ω–æ–≤–∞—è —Å—Ç—Ä–æ–∫–∞"#);
    println!(r#"  \r  - –≤–æ–∑–≤—Ä–∞—Ç –∫–∞—Ä–µ—Ç–∫–∏"#);
    println!(r#"  \t  - —Ç–∞–±—É–ª—è—Ü–∏—è"#);
    println!(r#"  \\  - –æ–±—Ä–∞—Ç–Ω—ã–π —Å–ª—ç—à"#);
    println!(r#"  \"  - –¥–≤–æ–π–Ω–∞—è –∫–∞–≤—ã—á–∫–∞"#);
    println!(r#"  \'  - –æ–¥–∏–Ω–∞—Ä–Ω–∞—è –∫–∞–≤—ã—á–∫–∞"#);
    println!(r#"  \0  - –Ω—É–ª–µ–≤–æ–π –±–∞–π—Ç"#);

    // Unicode –≤ —Å—Ç—Ä–æ–∫–∞—Ö
    println!("\n‚ïê‚ïê‚ïê Unicode –≤ —Å—Ç—Ä–æ–∫–∞—Ö ‚ïê‚ïê‚ïê");
    let unicode_string = "–ü—Ä–∏–≤–µ—Ç üåç! Hello ‰∏ñÁïå!";
    println!("  {}", unicode_string);
    println!("  –î–ª–∏–Ω–∞ –≤ –±–∞–π—Ç–∞—Ö: {}", unicode_string.len());
    println!("  –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤: {}", unicode_string.chars().count());

    // –ú–µ—Ç–æ–¥—ã —Å—Ç—Ä–æ–∫
    println!("\n‚ïê‚ïê‚ïê –ü–æ–ª–µ–∑–Ω—ã–µ –º–µ—Ç–æ–¥—ã —Å—Ç—Ä–æ–∫ ‚ïê‚ïê‚ïê");
