    let second = "World";
    let combined = format!("{} {}", first, second);
    println!("  format!(\"{{}} {{}}\", \"{}\", \"{}\")", first, second);
    println!("  –†–µ–∑—É–ª—å—Ç–∞—Ç: \"{}\"", combined);

