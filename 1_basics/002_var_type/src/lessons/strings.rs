    println!("  language: \"{}\"", language);

    // –ú–Ω–æ–≥–æ—Å—Ç—Ä–æ—á–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏
    println!("\n‚ïê‚ïê‚ïê –ú–Ω–æ–≥–æ—Å—Ç—Ä–æ—á–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏ ‚ïê‚ïê‚ïê");
    let multiline = "–≠—Ç–æ –ø–µ—Ä–≤–∞—è —Å—Ç—Ä–æ–∫–∞
–≠—Ç–æ –≤—Ç–æ—Ä–∞—è —Å—Ç—Ä–æ–∫–∞
–≠—Ç–æ —Ç—Ä–µ—Ç—å—è —Å—Ç—Ä–æ–∫–∞";
