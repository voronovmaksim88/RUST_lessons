    println!("  &'static str: \"{}\"", static_str);
    println!("  –•—Ä–∞–Ω–∏—Ç—Å—è –≤ –±–∏–Ω–∞—Ä–Ω–∏–∫–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã");
    println!("  –ñ–∏–≤—ë—Ç –≤—Å—ë –≤—Ä–µ–º—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –ø—Ä–æ–≥—Ä–∞–º–º—ã");

    println!("\nüìù –í–∞–∂–Ω—ã–µ –º–æ–º–µ–Ω—Ç—ã:");
    println!("  ‚Ä¢ &str - —ç—Ç–æ —Å—Ä–µ–∑ (view) —Å—Ç—Ä–æ–∫–æ–≤—ã—Ö –¥–∞–Ω–Ω—ã—Ö, –Ω–µ –≤–ª–∞–¥–µ–ª–µ—Ü");
    println!("  ‚Ä¢ –°—Ç—Ä–æ–∫–∏ –≤ Rust –≤—Å–µ–≥–¥–∞ –≤–∞–ª–∏–¥–Ω—ã–π UTF-8");
    println!("  ‚Ä¢ –ò–Ω–¥–µ–∫—Å–∞—Ü–∏—è –ø–æ –±–∞–π—Ç–∞–º, –Ω–µ –ø–æ —Å–∏–º–≤–æ–ª–∞–º: text[0] - –û–®–ò–ë–ö–ê!");
