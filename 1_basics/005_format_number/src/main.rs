fn main() {
    // Числа в Rust могут быть записаны в различных форматах

    // ============ ДВОИЧНЫЙ ФОРМАТ (binary) ============
    // Префикс: 0b
    let number: u8 = 0b1000_0001; // Двоичное число 10000001 (129 в десятичной)
    println!("Двоичное число 0b1000_0001 = {}", number);

    // Символ подчёркивания (_) может использоваться для улучшения читаемости
    let byte: u8 = 0b0101_1010; // Группируем по 4 бита для наглядности
    println!("Двоичное число 0b0101_1010 = {}", byte);

    // ============ ВОСЬМЕРИЧНЫЙ ФОРМАТ (octal) ============
    // Префикс: 0o
    let number: u8 = 0o10; // Восьмеричное число 10 (8 в десятичной)
    println!("Восьмеричное число 0o10 = {}", number);

    let permissions: u32 = 0o755; // Часто используется для прав доступа в Unix (rwxr-xr-x)
    println!("Восьмеричное число 0o755 = {}", permissions);

    // ============ ШЕСТНАДЦАТЕРИЧНЫЙ ФОРМАТ (hexadecimal) ============
    // Префикс: 0x
    let number: u8 = 0x10; // Шестнадцатеричное число 10 (16 в десятичной)
    println!("Шестнадцатеричное число 0x10 = {}", number);

    let color: u32 = 0xFF_00_FF; // Представление цвета в RGB: фиолетовый
    println!("Шестнадцатеричный цвет 0xFF00FF = {}", color);

    // ============ ФОРМАТИРОВАННЫЙ ВЫВОД ============
    // Rust позволяет выводить числа в разных форматах с помощью {}
    let value = 42;
    println!("Десятичный формат: {}", value);
    println!("Двоичный формат: {:b}", value); // Выводит 101010
    println!("Восьмеричный формат: {:o}", value); // Выводит 52
    println!("Шестнадцатеричный формат: {:x}", value); // Выводит 2a (строчные буквы)
    println!("Шестнадцатеричный формат (прописные): {:X}", value); // Выводит 2A

    // Можно добавлять префикс и дополнять нулями
    println!("С префиксом: {:#x}", value); // Выводит 0x2a
    println!("С нулями (8 знаков): {:08b}", value); // Выводит 00101010

    // ============ БОЛЬШИЕ ЧИСЛА ============
    // Использование подчёркивания делает большие числа более читаемыми
    let billion: u64 = 1_000_000_000;
    println!("Миллиард: {}", billion);

    let binary_large: u32 = 0b1010_1010_1010_1010;
    println!("Большое двоичное число: {}", binary_large);

    let hex_addr: usize = 0xDEAD_BEEF;
    println!("Шестнадцатеричный адрес: {:#x}", hex_addr);
}
