//! Демонстрация замыканий (безымянных функций) в Rust.
//!
//! Этот проект содержит примеры использования замыканий в Rust, разделённые на модули:
//! - [`basic_syntax`] — базовый синтаксис замыканий
//! - [`capture`] — захват переменных из окружения
//! - [`iterators`] — работа с итераторами
//! - [`traits`] — типы замыканий (Fn, FnMut, FnOnce)
//! - [`patterns`] — паттерны использования

pub mod basic_syntax;
pub mod capture;
pub mod iterators;
pub mod patterns;
pub mod traits;

fn main() {
    println!("╔═══════════════════════════════════════════════════════════╗");
    println!("║         ЗАМЫКАНИЯ (CLOSURES) В RUST — ДЕМОСТРАЦИЯ         ║");
    println!("╚═══════════════════════════════════════════════════════════╝");

    // Модуль 1: Базовый синтаксис
    basic_syntax::run_all();

    // Модуль 2: Захват переменных
    capture::run_all();

    // Модуль 3: Итераторы
    iterators::run_all();

    // Модуль 4: Типы замыканий
    traits::run_all();

    // Модуль 5: Паттерны
    patterns::run_all();

    println!("\n╔═══════════════════════════════════════════════════════════╗");
    println!("║            ВСЕ ПРИМЕРЫ ВЫПОЛНЕНЫ УСПЕШНО! ✓               ║");
    println!("╚═══════════════════════════════════════════════════════════╝");
}
